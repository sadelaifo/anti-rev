/* ------------------------------------------------------------------ */

#define MAGIC        "ANTREV01"
#define MAGIC_LEN    8
#define KEY_SIZE     32   /* AES-256 */
#define IV_SIZE      12   /* GCM nonce */
#define TAG_SIZE     16   /* GCM auth tag */
#define MAX_NAME     255
#define MAX_FILES    1024
#define CHUNK_SIZE   (16 * 1024 * 1024)  /* 16 MB I/O + crypto buffer */
#define SCM_BATCH    250  /* max fds per SCM_RIGHTS (kernel SCM_MAX_FD = 253) */

/* ------------------------------------------------------------------ */
/*  Per-file metadata collected during header scan (Phase 1)          */
/* ------------------------------------------------------------------ */
typedef struct {
    char     name[MAX_NAME + 1];
    int      is_main;
    uint8_t  iv[IV_SIZE];
    uint8_t  tag[TAG_SIZE];
    uint64_t ct_size;
    uint64_t ct_offset;   /* byte offset in /proc/self/exe */
} file_entry_t;

/* ------------------------------------------------------------------ */
/*  Little-endian readers                                              */
/* ------------------------------------------------------------------ */
static inline uint16_t u16le(const uint8_t *p)
{
    return (uint16_t)(p[0] | (p[1] << 8));
}
static inline uint32_t u32le(const uint8_t *p)
{
    return p[0] | ((uint32_t)p[1] << 8) | ((uint32_t)p[2] << 16) | ((uint32_t)p[3] << 24);
}
static inline uint64_t u64le(const uint8_t *p)
{
    uint64_t v = 0;
    for (int i = 0; i < 8; i++) v |= (uint64_t)p[i] << (8 * i);
    return v;
}

/* ------------------------------------------------------------------ */
/*  Write bytes to a new memfd (no O_CLOEXEC — must survive fexecve)  */
/* ------------------------------------------------------------------ */
static int make_memfd(const char *name)
{
    int fd = (int)syscall(__NR_memfd_create, name, 0 /* no MFD_CLOEXEC */);
    if (fd < 0) { perror("memfd_create"); return -1; }
    return fd;
}

static int write_chunk(int fd, const uint8_t *data, size_t len)
{
    for (size_t w = 0; w < len; ) {
        ssize_t n = write(fd, data + w, len - w);
        if (n < 0) { perror("write memfd"); return -1; }
        w += (size_t)n;
    }
    return 0;
}

#define BFLAG_HAS_LIBS     0x01
#define BFLAG_DAEMON_LIBS  0x02  /* libs served by external daemon */
#define BFLAG_WRAPPER      0x04  /* wrapper mode: exec argv[1...] with libs */

/* Max size of the daemon protocol data payload (per batch):
 * 4 bytes (nlibs) + SCM_BATCH * (2 + MAX_NAME) */
#define DAEMON_DATA_MAX  (4 + SCM_BATCH * (2 + MAX_NAME))

/* ------------------------------------------------------------------ */
/*  Daemon: serve lib memfds to sibling processes via SCM_RIGHTS       */
/*                                                                     */
/*  Protocol: data payload contains lib count + names, ancillary data  */
/*  carries the fds via SCM_RIGHTS.                                    */
/*                                                                     */
/*  Data layout:                                                       */
/*    [uint32_t nlibs]                                                 */
/*    for each lib:                                                    */
/*      [uint16_t name_len] [char name[name_len]]                     */
/*                                                                     */
/*  Ancillary: SCM_RIGHTS with nlibs file descriptors.                 */
/* ------------------------------------------------------------------ */

/* Build the data payload (lib count + names). Returns payload size. */
static size_t build_daemon_payload(uint8_t *buf, size_t buflen,
                                   const char (*names)[MAX_NAME + 1],
                                   int nlibs)
{
    size_t off = 0;
    uint32_t nl = (uint32_t)nlibs;
    if (off + 4 > buflen) return 0;
    memcpy(buf + off, &nl, 4);
    off += 4;

    for (int i = 0; i < nlibs; i++) {
        uint16_t nlen = (uint16_t)strlen(names[i]);
        if (off + 2 + nlen > buflen) return 0;
        memcpy(buf + off, &nlen, 2);
        off += 2;
        memcpy(buf + off, names[i], nlen);
        off += nlen;
    }
    return off;
}

static void daemon_serve(int listen_fd, const int *lib_fds,
                         const char (*lib_names)[MAX_NAME + 1], int nlibs)
{
    uid_t my_uid = getuid();
    signal(SIGPIPE, SIG_IGN);
    close(STDIN_FILENO);
    close(STDOUT_FILENO);
    close(STDERR_FILENO);

    for (;;) {
        int client = accept(listen_fd, NULL, NULL);
        if (client < 0) continue;

        /* Only serve same uid */
        struct ucred cred;
        socklen_t clen = sizeof(cred);
        if (getsockopt(client, SOL_SOCKET, SO_PEERCRED, &cred, &clen) < 0
            || cred.uid != my_uid) {
            close(client);
            continue;
        }

        /* Send libs in batches of SCM_BATCH (kernel limit: 253 fds) */
        int ok = 1;
        for (int off = 0; off < nlibs && ok; off += SCM_BATCH) {
            int batch = nlibs - off;
            if (batch > SCM_BATCH) batch = SCM_BATCH;

            uint8_t payload[DAEMON_DATA_MAX];
            size_t payload_len = build_daemon_payload(
                payload, sizeof(payload), lib_names + off, batch);

            struct iovec iov = { .iov_base = payload, .iov_len = payload_len };

            char cmsg_buf[CMSG_SPACE(SCM_BATCH * sizeof(int))];
            memset(cmsg_buf, 0, sizeof(cmsg_buf));

            struct msghdr msg = {0};
            msg.msg_iov        = &iov;
            msg.msg_iovlen     = 1;
            msg.msg_control    = cmsg_buf;
            msg.msg_controllen = CMSG_SPACE((size_t)batch * sizeof(int));

            struct cmsghdr *cmsg = CMSG_FIRSTHDR(&msg);
            cmsg->cmsg_level = SOL_SOCKET;
            cmsg->cmsg_type  = SCM_RIGHTS;
            cmsg->cmsg_len   = CMSG_LEN((size_t)batch * sizeof(int));
            memcpy(CMSG_DATA(cmsg), lib_fds + off, (size_t)batch * sizeof(int));

            if (sendmsg(client, &msg, 0) < 0) ok = 0;
        }

        /* Sentinel: nlibs=0 signals end of batches */
        uint32_t sentinel = 0;
        (void)send(client, &sentinel, sizeof(sentinel), 0);

        close(client);
    }
}

/* Receive lib fds + names from the daemon (batched protocol).
 * Daemon sends batches of up to SCM_BATCH fds, terminated by a
 * sentinel message (nlibs=0).
 * Returns 0 on success, -1 on failure. */
static int receive_lib_fds(int sd, int *out_fds,
                           char (*out_names)[MAX_NAME + 1], int *out_nlibs)
{
    *out_nlibs = 0;

    for (;;) {
        uint8_t payload[DAEMON_DATA_MAX];
        struct iovec iov = { .iov_base = payload, .iov_len = sizeof(payload) };

        char cmsg_buf[CMSG_SPACE(SCM_BATCH * sizeof(int))];
        memset(cmsg_buf, 0, sizeof(cmsg_buf));

        struct msghdr msg = {0};
        msg.msg_iov        = &iov;
        msg.msg_iovlen     = 1;
        msg.msg_control    = cmsg_buf;
        msg.msg_controllen = sizeof(cmsg_buf);

        ssize_t got = recvmsg(sd, &msg, 0);
        if (got < 4) return -1;

        uint32_t nl;
        memcpy(&nl, payload, 4);
        if (nl == 0) break;  /* sentinel — no more batches */
        if (*out_nlibs + (int)nl > MAX_FILES) return -1;

        struct cmsghdr *cmsg = CMSG_FIRSTHDR(&msg);
        if (!cmsg || cmsg->cmsg_type != SCM_RIGHTS) return -1;

        int nfds = (int)((cmsg->cmsg_len - CMSG_LEN(0)) / sizeof(int));
        if (nfds != (int)nl) return -1;

        memcpy(out_fds + *out_nlibs, CMSG_DATA(cmsg),
               (size_t)nfds * sizeof(int));

        /* Parse lib names for this batch */
        size_t off = 4;
        for (int i = 0; i < (int)nl; i++) {
            if (off + 2 > (size_t)got) return -1;
            uint16_t nlen;
            memcpy(&nlen, payload + off, 2);
            off += 2;
            if (nlen > MAX_NAME || off + nlen > (size_t)got) return -1;
            memcpy(out_names[*out_nlibs + i], payload + off, nlen);
            out_names[*out_nlibs + i][nlen] = '\0';
            off += nlen;
        }

        *out_nlibs += (int)nl;
    }

    return (*out_nlibs > 0) ? 0 : -1;
}

/* Build abstract socket address from key hash.
 * Returns the sockaddr_un length to pass to bind/connect. */
static socklen_t make_sock_addr(struct sockaddr_un *addr,
                                const uint8_t key[KEY_SIZE])
{
    /* Derive socket name: AES_K(0^128)[0:8] → hex.
     * This is the GHASH subkey H, deterministic per key. */
    aes256gcm_ctx tmp;
    uint8_t dummy_iv[IV_SIZE] = {0};
    aes256gcm_init(&tmp, key, dummy_iv);
    /* tmp.H now contains AES_K(0^128) */

    char hex[17];
    for (int i = 0; i < 8; i++)
        snprintf(hex + i * 2, 3, "%02x", tmp.H[i]);

    memset(addr, 0, sizeof(*addr));
    addr->sun_family = AF_UNIX;
    /* Abstract socket: sun_path[0] = '\0' (already zeroed), name follows */
    snprintf(addr->sun_path + 1, sizeof(addr->sun_path) - 1,
             "antirev_%s", hex);

    explicit_bzero(&tmp, sizeof(tmp));

    return (socklen_t)(offsetof(struct sockaddr_un, sun_path)
                       + 1 + strlen(addr->sun_path + 1));
}

/* ------------------------------------------------------------------ */
/*  Decrypt standalone encrypted file from disk                        */
/*  Format: ANTREV01(8) + iv(12) + tag(16) + ciphertext               */
/*  Returns memfd on success, -1 on failure.                           */
/* ------------------------------------------------------------------ */
static int decrypt_enc_file(const char *path, const uint8_t *key,
                            uint8_t *chunk)
{
    int fd = open(path, O_RDONLY);
    if (fd < 0) return -1;

    uint8_t hdr[MAGIC_LEN + IV_SIZE + TAG_SIZE]; /* 36 bytes */
    if (pread(fd, hdr, sizeof(hdr), 0) != (ssize_t)sizeof(hdr)
        || memcmp(hdr, MAGIC, MAGIC_LEN) != 0) {
        close(fd);
        return -1;
    }

    uint8_t *iv  = hdr + MAGIC_LEN;
    uint8_t *tag = hdr + MAGIC_LEN + IV_SIZE;

    off_t fsize = lseek(fd, 0, SEEK_END);
    off_t ct_off = (off_t)sizeof(hdr);
    uint64_t ct_size = (uint64_t)(fsize - ct_off);
    if (ct_size == 0) { close(fd); return -1; }

    const char *bname = strrchr(path, '/');
    bname = bname ? bname + 1 : path;

    int mfd = make_memfd(bname);
    if (mfd < 0) { close(fd); return -1; }

    aes256gcm_ctx ctx;

    /* Single pass: GHASH + CTR simultaneously (halves disk I/O) */
    aes256gcm_init(&ctx, key, iv);
    uint64_t remaining = ct_size;
    off_t pos = ct_off;
    while (remaining > 0) {
        size_t  to_read = (remaining < CHUNK_SIZE) ? (size_t)remaining : CHUNK_SIZE;
        ssize_t got     = pread(fd, chunk, to_read, pos);
        if (got != (ssize_t)to_read) { close(fd); close(mfd); return -1; }
        aes256gcm_onepass(&ctx, chunk, chunk, to_read);
        if (write_chunk(mfd, chunk, to_read) != 0) { close(fd); close(mfd); return -1; }
        pos       += (off_t)to_read;
        remaining -= to_read;
    }
    if (aes256gcm_ghash_verify(&ctx, tag) != 0) {
        if (ftruncate(mfd, 0)) { /* wipe plaintext */ }
        close(fd); close(mfd);
        return -1;
    }
    if (lseek(mfd, 0, SEEK_SET) < 0) { close(fd); close(mfd); return -1; }

    close(fd);
    return mfd;
}

/* ------------------------------------------------------------------ */
/*  Parallel decryption of encrypted .so files from disk               */
/* ------------------------------------------------------------------ */

/* Per-file decryption job */
typedef struct {
    char     path[4096];
    char     name[MAX_NAME + 1];
    const uint8_t *key;
    int      fd;      /* result memfd, -1 on failure */
} dec_job_t;

/* Work-stealing pool: each thread grabs next available job */
typedef struct {
    dec_job_t   *jobs;
    int          njobs;
    volatile int next;   /* atomic counter for work stealing */
} dec_pool_t;

static void *pool_worker(void *arg)
{
    dec_pool_t *pool = (dec_pool_t *)arg;
    uint8_t *chunk = malloc(CHUNK_SIZE);
    if (!chunk) return NULL;

    for (;;) {
        int idx = __sync_fetch_and_add(&pool->next, 1);
        if (idx >= pool->njobs) break;
        pool->jobs[idx].fd = decrypt_enc_file(pool->jobs[idx].path,
                                               pool->jobs[idx].key, chunk);
    }

    explicit_bzero(chunk, CHUNK_SIZE);
    free(chunk);
    return NULL;
}

/* Recursively collect paths of encrypted .so files (ANTREV01 magic) */
static void collect_enc_paths(const char *dir, dec_job_t *jobs, int *njobs,
                              int max)
{
    DIR *dp = opendir(dir);
    if (!dp) return;

    struct dirent *de;
    while ((de = readdir(dp)) != NULL && *njobs < max) {
        if (de->d_name[0] == '.') continue;

        char path[4096];
        snprintf(path, sizeof(path), "%s/%s", dir, de->d_name);

        struct stat st;
        if (lstat(path, &st) != 0) continue;
        if (S_ISLNK(st.st_mode)) continue;  /* skip symlinks — avoid dupes */

        if (S_ISDIR(st.st_mode)) {
            collect_enc_paths(path, jobs, njobs, max);
            continue;
        }
        if (!S_ISREG(st.st_mode)) continue;
        if (!strstr(de->d_name, ".so")) continue;

        /* Skip duplicate filenames (same lib in different subdirs) */
        int dup = 0;
        for (int i = 0; i < *njobs; i++) {
            if (strcmp(jobs[i].name, de->d_name) == 0) { dup = 1; break; }
        }
        if (dup) continue;

        /* Quick magic check — avoids queuing non-encrypted files */
        int fd = open(path, O_RDONLY);
        if (fd < 0) continue;
        uint8_t magic[MAGIC_LEN];
        int is_enc = (read(fd, magic, MAGIC_LEN) == MAGIC_LEN
                      && memcmp(magic, MAGIC, MAGIC_LEN) == 0);
        close(fd);
        if (!is_enc) continue;

        snprintf(jobs[*njobs].path, sizeof(jobs[0].path), "%s", path);
        snprintf(jobs[*njobs].name, sizeof(jobs[0].name), "%s", de->d_name);
        jobs[*njobs].fd = -1;
        (*njobs)++;
    }
    closedir(dp);
}

/* Scan directory, decrypt all encrypted libs in parallel using pthreads */
static int scan_encrypted_libs(const char *exe_path, const uint8_t *key,
                               int *lib_fds, char (*lib_names)[MAX_NAME + 1],
                               int *nlibs)
{
    char dir[4096];
    const char *slash = strrchr(exe_path, '/');
    if (slash) {
        size_t dlen = (size_t)(slash - exe_path);
        if (dlen >= sizeof(dir)) return -1;
        memcpy(dir, exe_path, dlen);
        dir[dlen] = '\0';
    } else {
        dir[0] = '.'; dir[1] = '\0';
    }

    /* Phase 1: collect encrypted file paths */
    dec_job_t *jobs = calloc(MAX_FILES, sizeof(dec_job_t));
    if (!jobs) return -1;
    int njobs = 0;
    collect_enc_paths(dir, jobs, &njobs, MAX_FILES);

    if (njobs == 0) { free(jobs); return 0; }

    /* Phase 2: decrypt in parallel */
    long ncpus = sysconf(_SC_NPROCESSORS_ONLN);
    if (ncpus < 1) ncpus = 4;
    int nthreads = (njobs < (int)ncpus) ? njobs : (int)ncpus;

    for (int i = 0; i < njobs; i++)
        jobs[i].key = key;

    dec_pool_t pool = { .jobs = jobs, .njobs = njobs, .next = 0 };
    pthread_t *tids = calloc((size_t)nthreads, sizeof(pthread_t));
    if (!tids) { free(jobs); return -1; }

    for (int t = 0; t < nthreads; t++)
        pthread_create(&tids[t], NULL, pool_worker, &pool);
    for (int t = 0; t < nthreads; t++)
        pthread_join(tids[t], NULL);

    /* Phase 3: collect results */
    for (int i = 0; i < njobs && *nlibs < MAX_FILES; i++) {
        if (jobs[i].fd >= 0) {
            lib_fds[*nlibs] = jobs[i].fd;
            memcpy(lib_names[*nlibs], jobs[i].name, MAX_NAME + 1);
            (*nlibs)++;
            fprintf(stderr, "[antirev] decrypted: %s\n", jobs[i].name);
        }
    }

    free(tids);
    free(jobs);
    return 0;
}

/* ------------------------------------------------------------------ */
/*  main                                                               */
/* ------------------------------------------------------------------ */
int main(int argc __attribute__((unused)), char *argv[], char *envp[])
{
    /* 1. Capture real exe path before fexecve replaces /proc/self/exe.
     *    Use raw syscall to bypass any inherited LD_PRELOAD exe_shim from
     *    a parent protected binary — we need the true kernel-level path. */
    char real_exe[4096];
    {
        ssize_t n = (ssize_t)syscall(SYS_readlinkat, AT_FDCWD,
                                     "/proc/self/exe", real_exe, sizeof(real_exe) - 1);
        if (n <= 0) { perror("readlink /proc/self/exe"); return 1; }
        real_exe[n] = '\0';
    }

    /* 2. Open /proc/self/exe and read the 48-byte trailer */
    int self = open("/proc/self/exe", O_RDONLY);
    if (self < 0) { perror("open /proc/self/exe"); return 1; }

    off_t fsize = lseek(self, 0, SEEK_END);
    if (fsize < 48) {
        fprintf(stderr, "[antirev] binary too small\n");
        return 1;
    }

    /* Trailer layout: [bundle_offset:8B][key:32B][magic:8B] = 48 bytes */
    uint8_t trailer[48];
    if (pread(self, trailer, 48, fsize - 48) != 48) {
        perror("pread trailer"); return 1;
    }
    if (memcmp(trailer + 40, MAGIC, MAGIC_LEN) != 0) {
        fprintf(stderr, "[antirev] not a protected binary (magic not found)\n");
        return 1;
    }

    uint64_t bundle_off = u64le(trailer);

    /* 2. Extract key from trailer */
    uint8_t key[KEY_SIZE];
    memcpy(key, trailer + 8, KEY_SIZE);

    /* ----------------------------------------------------------------
     * Phase 1: scan bundle headers — collect metadata for every file.
     * We only read the tiny per-file headers; ciphertext is skipped via
     * offset arithmetic and never loaded into user-space memory here.
     * ---------------------------------------------------------------- */
    uint8_t tmp[2 + MAX_NAME + 1 + IV_SIZE + TAG_SIZE + 8]; /* max header */

    /* Read num_files (4 bytes) + bundle_flags (1 byte) */
    if (pread(self, tmp, 5, (off_t)bundle_off) != 5) {
        perror("pread num_files"); return 1;
    }
    uint32_t nfiles = u32le(tmp);
    uint8_t bundle_flags = tmp[4];
    if (nfiles > MAX_FILES) {
        fprintf(stderr, "[antirev] too many files in bundle (%u)\n", nfiles);
        return 1;
    }

    file_entry_t entries[MAX_FILES];
    off_t scan = (off_t)bundle_off + 5;

    for (uint32_t i = 0; i < nfiles; i++) {
        file_entry_t *e = &entries[i];

        /* name_len (2 bytes) */
        if (pread(self, tmp, 2, scan) != 2) { perror("pread name_len"); return 1; }
        uint16_t nlen = u16le(tmp);
        if (nlen > MAX_NAME) {
            fprintf(stderr, "[antirev] name too long in bundle\n");
            return 1;
        }
        scan += 2;

        /* name + flags + iv + tag + ct_size */
        ssize_t hdr_rest = nlen + 1 + IV_SIZE + TAG_SIZE + 8;
        if (pread(self, tmp, (size_t)hdr_rest, scan) != hdr_rest) {
            perror("pread file header"); return 1;
        }
        memcpy(e->name, tmp, nlen);
        e->name[nlen]  = '\0';
        e->is_main     = tmp[nlen] & 1;
        memcpy(e->iv,  tmp + nlen + 1,                     IV_SIZE);
        memcpy(e->tag, tmp + nlen + 1 + IV_SIZE,           TAG_SIZE);
        e->ct_size     = u64le(tmp + nlen + 1 + IV_SIZE + TAG_SIZE);
        scan          += hdr_rest;

        e->ct_offset   = (uint64_t)scan;
        scan          += (off_t)e->ct_size;

        /* Sanity: ciphertext must not overlap the trailer */
        if (e->ct_offset + e->ct_size > (uint64_t)(fsize - 48)) {
            fprintf(stderr, "[antirev] bundle extends past trailer\n");
            return 1;
        }
    }

    /* Parse needed-libs section (after file entries, when BFLAG_DAEMON_LIBS).
     * Format: [num_needed:2B] [name_len:2B name:...]... */
    int n_needed = 0;
    int has_needed_section = 0;
    char needed_names[MAX_FILES][MAX_NAME + 1];

    if (bundle_flags & BFLAG_DAEMON_LIBS) {
        has_needed_section = 1;
        uint8_t nbuf[2];
        if (pread(self, nbuf, 2, scan) == 2) {
            uint16_t nn = u16le(nbuf);
            scan += 2;
            for (int i = 0; i < (int)nn && i < MAX_FILES; i++) {
                if (pread(self, nbuf, 2, scan) != 2) break;
                uint16_t nl = u16le(nbuf);
                scan += 2;
                if (nl > MAX_NAME) break;
                if (pread(self, needed_names[n_needed], nl, scan) != nl) break;
                needed_names[n_needed][nl] = '\0';
                n_needed++;
                scan += nl;
            }
        }
    }

    /* ----------------------------------------------------------------
     * Phase 2: for each file, single-pass GHASH + CTR decryption.
     * Reads ciphertext once, writes plaintext to memfd, then verifies
     * the authentication tag.  On auth failure, plaintext is wiped.
     *
     * Peak user-space heap: one CHUNK_SIZE buffer.
     * ---------------------------------------------------------------- */
    uint8_t *chunk = malloc(CHUNK_SIZE);
    if (!chunk) { perror("malloc chunk"); return 1; }

    int main_fd = -1;
    int lib_fds[MAX_FILES];
    char lib_names[MAX_FILES][MAX_NAME + 1];
    int nlibs = 0;

    for (uint32_t i = 0; i < nfiles; i++) {
        file_entry_t *e = &entries[i];
        aes256gcm_ctx ctx;

        /* Single pass: GHASH + CTR simultaneously (halves I/O) */
        int fd = make_memfd(e->name);
        if (fd < 0) return 1;

        aes256gcm_init(&ctx, key, e->iv);
        uint64_t remaining = e->ct_size;
        off_t    pos       = (off_t)e->ct_offset;
        while (remaining > 0) {
            size_t  to_read = (remaining < CHUNK_SIZE) ? (size_t)remaining : CHUNK_SIZE;
            ssize_t got     = pread(self, chunk, to_read, pos);
            if (got != (ssize_t)to_read) { perror("pread ciphertext"); return 1; }
            aes256gcm_onepass(&ctx, chunk, chunk, to_read);
            if (write_chunk(fd, chunk, to_read) != 0) return 1;
            pos       += (off_t)to_read;
            remaining -= to_read;
        }
        if (aes256gcm_ghash_verify(&ctx, e->tag) != 0) {
            fprintf(stderr, "[antirev] decryption failed for '%s'\n", e->name);
            if (ftruncate(fd, 0)) { /* wipe plaintext */ }
            close(fd);
            return 1;
        }
        if (lseek(fd, 0, SEEK_SET) < 0) { perror("lseek memfd"); return 1; }

        if (e->is_main) {
            main_fd = fd;
        } else {
            memcpy(lib_names[nlibs], e->name, MAX_NAME + 1);
            lib_fds[nlibs++] = fd;
        }
    }

    /* Done reading from the binary */
    explicit_bzero(chunk, CHUNK_SIZE);
    free(chunk);
    close(self);

    /* ----------------------------------------------------------------
     * Mode B: Daemon-only (no main binary).
     * If libs were bundled, they're already decrypted above.
     * If not (lightweight daemon), scan disk for encrypted .so files.
     * Then bind abstract socket and serve lib fds forever.
     * ---------------------------------------------------------------- */
    if (main_fd < 0 && !(bundle_flags & BFLAG_WRAPPER)) {
        /* Raise fd limit: daemon holds ~nlibs memfds + listen socket */
        struct rlimit rl;
        if (getrlimit(RLIMIT_NOFILE, &rl) == 0 && rl.rlim_cur < rl.rlim_max) {
            rl.rlim_cur = rl.rlim_max;
            setrlimit(RLIMIT_NOFILE, &rl);
        }

        struct timespec t_start, t_end;
        clock_gettime(CLOCK_MONOTONIC, &t_start);

        if (nlibs == 0) {
            /* Lightweight daemon — scan for encrypted libs on disk */
            scan_encrypted_libs(real_exe, key, lib_fds, lib_names, &nlibs);
        }
        if (nlibs == 0) {
            fprintf(stderr, "[antirev] no main binary and no libs found\n");
            return 1;
        }

        clock_gettime(CLOCK_MONOTONIC, &t_end);
        double elapsed = (double)(t_end.tv_sec - t_start.tv_sec)
                       + (double)(t_end.tv_nsec - t_start.tv_nsec) / 1e9;
        fprintf(stderr, "[antirev] decrypted %d libs in %.1fs\n",
                nlibs, elapsed);

        struct sockaddr_un addr;
        socklen_t addr_len = make_sock_addr(&addr, key);
        explicit_bzero(key, sizeof(key));

        int listen_sd = socket(AF_UNIX, SOCK_STREAM, 0);
        if (listen_sd < 0) { perror("socket"); return 1; }
        if (bind(listen_sd, (struct sockaddr *)&addr, addr_len) < 0) {
            perror("[antirev] daemon bind");
            return 1;
        }
        listen(listen_sd, 16);

        fprintf(stderr, "[antirev] lib daemon ready (%d libs)\n", nlibs);

        /* Daemonize: fork, parent exits, child serves */
        pid_t pid = fork();
        if (pid < 0) { perror("fork"); return 1; }
        if (pid > 0) {
            /* Parent exits successfully — daemon is running */
            _exit(0);
        }
        /* Child: become session leader, serve forever */
        setsid();
        daemon_serve(listen_sd, lib_fds, lib_names, nlibs);
        _exit(0);  /* unreachable */
    }

    /* ----------------------------------------------------------------
     * Mode D: Wrapper (BFLAG_WRAPPER).
     * No main binary — connect to daemon, receive lib fds, set up
     * LD_PRELOAD (dlopen_shim) + ANTIREV_FD_MAP (all libs), then
     * exec argv[1...].  The dlopen_shim constructor eagerly preloads
     * all ANTIREV_FD_MAP libs via retry loop.
     * ---------------------------------------------------------------- */
    if (bundle_flags & BFLAG_WRAPPER) {
        if (argc < 2) {
            fprintf(stderr, "Usage: %s <command> [args...]\n", argv[0]);
            return 1;
        }

        /* Raise fd limit: we'll receive ~nlibs fds via SCM_RIGHTS */
        struct rlimit rl;
        if (getrlimit(RLIMIT_NOFILE, &rl) == 0 && rl.rlim_cur < rl.rlim_max) {
            rl.rlim_cur = rl.rlim_max;
            setrlimit(RLIMIT_NOFILE, &rl);
        }

        /* Connect to daemon */
        struct sockaddr_un addr;
        socklen_t addr_len = make_sock_addr(&addr, key);
        explicit_bzero(key, sizeof(key));

        int connected = 0, daemon_launched = 0;
        for (int retry = 0; retry < 50; retry++) {
            int sd = socket(AF_UNIX, SOCK_STREAM, 0);
            if (sd >= 0 && connect(sd, (struct sockaddr *)&addr, addr_len) == 0) {
                char recv_names[MAX_FILES][MAX_NAME + 1];
                if (receive_lib_fds(sd, lib_fds, recv_names, &nlibs) == 0
                    && nlibs > 0) {
                    memcpy(lib_names, recv_names, sizeof(recv_names));
                    connected = 1;
                }
                close(sd);
                break;
            }
            if (sd >= 0) close(sd);

            if (!daemon_launched && retry == 0) {
                char daemon_path[4096];
                char *slash = strrchr(real_exe, '/');
                size_t dirlen = slash ? (size_t)(slash - real_exe) : 1;
                if (!slash) { daemon_path[0] = '.'; dirlen = 1; }
                else memcpy(daemon_path, real_exe, dirlen);
                snprintf(daemon_path + dirlen,
                         sizeof(daemon_path) - dirlen, "/.antirev-libd");
                if (access(daemon_path, X_OK) == 0) {
                    pid_t pid = fork();
                    if (pid == 0) {
                        char *dargv[] = { daemon_path, NULL };
                        execve(daemon_path, dargv, envp);
                        _exit(127);
                    }
                    if (pid > 0) { int st; waitpid(pid, &st, 0); daemon_launched = 1; }
                }
            }
            usleep(100000);
        }

        if (!connected || nlibs == 0) {
            fprintf(stderr, "[antirev] wrapper: could not get libs from daemon\n");
            return 1;
        }
        fprintf(stderr, "[antirev] wrapper: received %d libs\n", nlibs);

        /* Create dlopen_shim memfd */
        int dlopen_shim_fd = make_memfd("antirev_dlopen_shim.so");
        if (dlopen_shim_fd < 0) return 1;
        if (write_chunk(dlopen_shim_fd, dlopen_shim_blob, dlopen_shim_blob_len) != 0) return 1;
        if (lseek(dlopen_shim_fd, 0, SEEK_SET) < 0) { perror("lseek"); return 1; }

        /* Build ANTIREV_FD_MAP with ALL libs */
        size_t map_len = 16 + (size_t)nlibs * (MAX_NAME + 16);
        char *fd_map = malloc(map_len);
        if (!fd_map) { perror("malloc"); return 1; }
        int moff = snprintf(fd_map, map_len, "ANTIREV_FD_MAP=");
        for (int j = 0; j < nlibs; j++) {
            if (j > 0) fd_map[moff++] = ',';
            moff += snprintf(fd_map + moff, map_len - (size_t)moff,
                             "%s=%d", lib_names[j], lib_fds[j]);
        }

        /* Build LD_PRELOAD with only dlopen_shim */
        char ld_preload[128];
        snprintf(ld_preload, sizeof(ld_preload),
                 "LD_PRELOAD=/proc/self/fd/%d", dlopen_shim_fd);

        /* Build new environment */
        int envc = 0;
        while (envp[envc]) envc++;
        char **new_env = malloc((size_t)(envc + 3) * sizeof(char *));
        if (!new_env) { perror("malloc"); return 1; }
        int ei = 0;
        for (int j = 0; j < envc; j++) {
            if (strncmp(envp[j], "LD_PRELOAD=",   11) == 0) continue;
            if (strncmp(envp[j], "ANTIREV_FD_MAP=", 15) == 0) continue;
            new_env[ei++] = envp[j];
        }
        new_env[ei++] = ld_preload;
        new_env[ei++] = fd_map;
        new_env[ei]   = NULL;

        /* Exec the wrapped command */
        extern char **environ;
        environ = new_env;
        execvp(argv[1], &argv[1]);
        perror("execvp");
        return 1;
    }

    /* ----------------------------------------------------------------
     * Phase 3: lib fd acquisition.
     *
     * Mode C (daemon libs): no libs bundled, must receive from daemon.
     * ---------------------------------------------------------------- */
    if ((bundle_flags & BFLAG_DAEMON_LIBS) && nlibs == 0) {
        /* Mode C: client-only — receive libs from daemon.
         * If no daemon is running, auto-launch .antirev-libd from the
         * same directory as our binary (first process bootstraps it). */

        /* Raise fd limit: we'll receive ~nlibs fds via SCM_RIGHTS */
        struct rlimit rl;
        if (getrlimit(RLIMIT_NOFILE, &rl) == 0 && rl.rlim_cur < rl.rlim_max) {
            rl.rlim_cur = rl.rlim_max;
            setrlimit(RLIMIT_NOFILE, &rl);
        }

        struct sockaddr_un addr;
        socklen_t addr_len = make_sock_addr(&addr, key);

        int connected = 0;
        int daemon_launched = 0;

        for (int retry = 0; retry < 50; retry++) {
            int sd = socket(AF_UNIX, SOCK_STREAM, 0);
            if (sd >= 0 && connect(sd, (struct sockaddr *)&addr, addr_len) == 0) {
                char recv_names[MAX_FILES][MAX_NAME + 1];
                if (receive_lib_fds(sd, lib_fds, recv_names, &nlibs) == 0
                    && nlibs > 0) {
                    memcpy(lib_names, recv_names, sizeof(recv_names));
                    connected = 1;
                }
                close(sd);
                break;
            }
            if (sd >= 0) close(sd);

            /* First failure: try to auto-launch the daemon */
            if (!daemon_launched && retry == 0) {
                /* Build path: dirname(real_exe) + "/.antirev-libd" */
                char daemon_path[4096];
                {
                    char *slash = strrchr(real_exe, '/');
                    size_t dirlen = slash ? (size_t)(slash - real_exe) : 1;
                    if (!slash) { daemon_path[0] = '.'; dirlen = 1; }
                    else memcpy(daemon_path, real_exe, dirlen);
                    snprintf(daemon_path + dirlen,
                             sizeof(daemon_path) - dirlen,
                             "/.antirev-libd");
                }

                if (access(daemon_path, X_OK) == 0) {
                    pid_t pid = fork();
                    if (pid == 0) {
                        char *dargv[] = { daemon_path, NULL };
                        execve(daemon_path, dargv, envp);
                        _exit(127);
                    }
                    if (pid > 0) {
                        /* Wait for daemon to exit (it daemonizes itself,
                         * so the parent exits quickly) */
                        int st;
                        waitpid(pid, &st, 0);
                        daemon_launched = 1;
                    }
                }
            }

            usleep(100000); /* 100 ms — daemon may be starting */
        }
        if (!connected) {
            fprintf(stderr, "[antirev] failed to connect to lib daemon\n");
            return 1;
        }
    }

    /* Wipe key (after daemon socket name derivation) */
    explicit_bzero(key, sizeof(key));

    /* Phase 4. Write embedded shims to memfds */
    int exe_shim_fd = make_memfd("antirev_exe_shim.so");
    if (exe_shim_fd < 0) return 1;
    if (write_chunk(exe_shim_fd, exe_shim_blob, exe_shim_blob_len) != 0) return 1;
    if (lseek(exe_shim_fd, 0, SEEK_SET) < 0) { perror("lseek exe_shim"); return 1; }

    int dlopen_shim_fd = make_memfd("antirev_dlopen_shim.so");
    if (dlopen_shim_fd < 0) return 1;
    if (write_chunk(dlopen_shim_fd, dlopen_shim_blob, dlopen_shim_blob_len) != 0) return 1;
    if (lseek(dlopen_shim_fd, 0, SEEK_SET) < 0) { perror("lseek dlopen_shim"); return 1; }

    /* Phase 4b. Split libs into DT_NEEDED (LD_PRELOAD) and rest (ANTIREV_FD_MAP).
     * DT_NEEDED libs are preloaded for fast startup.
     * Remaining encrypted libs are available on-demand via dlopen_shim. */
    int preload_fds[MAX_FILES];
    char preload_names[MAX_FILES][MAX_NAME + 1];
    int n_preload = 0;

    int fdmap_fds[MAX_FILES];
    char fdmap_names[MAX_FILES][MAX_NAME + 1];
    int n_fdmap = 0;

    if (has_needed_section && nlibs > 0) {
        /* Build preload list in needed_names order (dependency-first).
         * The packer embeds needed_names with deepest deps first so that
         * glibc can resolve each lib's DT_NEEDED from already-loaded libs. */
        for (int k = 0; k < n_needed; k++) {
            for (int j = 0; j < nlibs; j++) {
                if (strcmp(lib_names[j], needed_names[k]) == 0) {
                    preload_fds[n_preload] = lib_fds[j];
                    memcpy(preload_names[n_preload], lib_names[j], MAX_NAME + 1);
                    n_preload++;
                    break;
                }
            }
        }
        /* Remaining libs go to ANTIREV_FD_MAP for on-demand dlopen */
        for (int j = 0; j < nlibs; j++) {
            int is_needed = 0;
            for (int k = 0; k < n_needed; k++) {
                if (strcmp(lib_names[j], needed_names[k]) == 0) {
                    is_needed = 1;
                    break;
                }
            }
            if (!is_needed) {
                fdmap_fds[n_fdmap] = lib_fds[j];
                memcpy(fdmap_names[n_fdmap], lib_names[j], MAX_NAME + 1);
                n_fdmap++;
            }
        }
        fprintf(stderr, "[antirev] split: %d on LD_PRELOAD, %d on FD_MAP "
                "(needed=%d, daemon=%d)\n",
                n_preload, n_fdmap, n_needed, nlibs);
        for (int k = 0; k < n_preload; k++)
            fprintf(stderr, "[antirev]   PRELOAD: %s (fd %d)\n",
                    preload_names[k], preload_fds[k]);
    } else {
        /* No needed list — all libs go on LD_PRELOAD (backward compat) */
        memcpy(preload_fds, lib_fds, (size_t)nlibs * sizeof(int));
        memcpy(preload_names, lib_names, (size_t)nlibs * (MAX_NAME + 1));
        n_preload = nlibs;
        fprintf(stderr, "[antirev] backward compat: all %d libs on LD_PRELOAD\n",
                nlibs);
    }

    /* Phase 5. Build new envp */
    int envc = 0;
    while (envp[envc]) envc++;

    /* Find existing LD_PRELOAD, stripping /proc/self/fd/ entries from
     * parent protected processes to prevent cascading duplicate loads. */
    char filtered_preload[8192];
    filtered_preload[0] = '\0';
    for (int j = 0; j < envc; j++) {
        if (strncmp(envp[j], "LD_PRELOAD=", 11) == 0) {
            const char *src = envp[j] + 11;
            int foff = 0;
            while (*src) {
                const char *end = src;
                while (*end && *end != ':') end++;
                size_t tlen = (size_t)(end - src);
                /* Keep only real file paths, not /proc/self/fd/ memfd refs */
                if (tlen > 0
                    && strncmp(src, "/proc/self/fd/", 14) != 0) {
                    if (foff > 0) filtered_preload[foff++] = ':';
                    memcpy(filtered_preload + foff, src, tlen);
                    foff += (int)tlen;
                }
                src = (*end == ':') ? end + 1 : end;
            }
            filtered_preload[foff] = '\0';
            break;
        }
    }
    const char *existing_preload = filtered_preload[0] ? filtered_preload : NULL;

    /* +6: REAL_EXE, MAIN_FD, LD_PRELOAD, ANTIREV_FD_MAP, NULL + spare */
    char **new_env = malloc((size_t)(envc + 6) * sizeof(char *));
    if (!new_env) { perror("malloc env"); return 1; }

    char real_exe_entry[4096 + 32];
    char main_fd_entry[32];

    snprintf(real_exe_entry, sizeof(real_exe_entry),
             "ANTIREV_REAL_EXE=%s", real_exe);
    snprintf(main_fd_entry, sizeof(main_fd_entry),
             "ANTIREV_MAIN_FD=%d", main_fd);

    /* Build LD_PRELOAD: exe_shim + dlopen_shim + DT_NEEDED lib memfds.
     * Each fd path is /proc/self/fd/NNN — at most ~20 chars each. */
    size_t preload_len = 128 + (size_t)n_preload * 24
                       + (existing_preload ? 1 + strlen(existing_preload) : 0);
    char *ld_preload_entry = malloc(preload_len);
    if (!ld_preload_entry) { perror("malloc ld_preload"); return 1; }

    int off = snprintf(ld_preload_entry, preload_len,
                       "LD_PRELOAD=/proc/self/fd/%d:/proc/self/fd/%d",
                       exe_shim_fd, dlopen_shim_fd);
    for (int j = 0; j < n_preload; j++)
        off += snprintf(ld_preload_entry + off, preload_len - (size_t)off,
                        ":/proc/self/fd/%d", preload_fds[j]);
    if (existing_preload)
        snprintf(ld_preload_entry + off, preload_len - (size_t)off,
                 ":%s", existing_preload);

    /* Build ANTIREV_FD_MAP: name=fd,name=fd,... for dlopen'd libs */
    char *fd_map_entry = NULL;
    if (n_fdmap > 0) {
        size_t map_len = 16 + (size_t)n_fdmap * (MAX_NAME + 16);
        fd_map_entry = malloc(map_len);
        if (fd_map_entry) {
            int moff = snprintf(fd_map_entry, map_len, "ANTIREV_FD_MAP=");
            for (int j = 0; j < n_fdmap; j++) {
                if (j > 0) fd_map_entry[moff++] = ',';
                moff += snprintf(fd_map_entry + moff, map_len - (size_t)moff,
                                 "%s=%d", fdmap_names[j], fdmap_fds[j]);
            }
        }
    }

    int ei = 0;
    for (int j = 0; j < envc; j++) {
        if (strncmp(envp[j], "LD_PRELOAD=",       11) == 0) continue;
        if (strncmp(envp[j], "ANTIREV_REAL_EXE=", 17) == 0) continue;
        if (strncmp(envp[j], "ANTIREV_MAIN_FD=",  16) == 0) continue;
        if (strncmp(envp[j], "ANTIREV_FD_MAP=",   15) == 0) continue;
        new_env[ei++] = envp[j];
    }
    new_env[ei++] = real_exe_entry;
    new_env[ei++] = main_fd_entry;
    new_env[ei++] = ld_preload_entry;
    if (fd_map_entry)
        new_env[ei++] = fd_map_entry;
    new_env[ei]   = NULL;

    /* Phase 6. Replace this process with the decrypted binary — no fork, same PID */
    fexecve(main_fd, argv, new_env);
    perror("fexecve");
    return 1;
}
