/* Reap stale /tmp/antirev_<pid>_XXXXXX dirs whose owner PID is dead.
 *
 * Each protected exe creates one of these on launch as its symlink
 * dir (Phase 4c).  exe_shim's atexit handler removes it on normal
 * exit; this sweep handles the SIGKILL / segfault / hard-crash case
 * that atexit can't.  The PID tag encodes ownership so we can decide
 * safely without coordinating with active exes.
 *
 * Called twice in the daemon's lifecycle: once at startup (catches
 * anything from prior crashed runs) and once on shutdown (catches
 * any of our session's exes that crashed before atexit could fire).
 *
 * Safe under PID reuse: in the unlikely case a recycled PID matches
 * a stale dir name, the new PID has nothing to do with that dir, so
 * removing it harms nothing. */
static void sweep_dead_symlink_dirs(void) {
    DIR *dp = opendir("/tmp");
    if (!dp) return;

    struct dirent *de;
    while ((de = readdir(dp)) != NULL) {
        if (strncmp(de->d_name, "antirev_", 8) != 0) continue;

        /* Parse "antirev_<pid>_<rand>" — accept the legacy
         * "antirev_<rand>" form too by skipping it (no PID = can't
         * decide ownership safely). */
        const char *p = de->d_name + 8;
        char *endp;
        long pid = strtol(p, &endp, 10);
        if (endp == p || *endp != '_' || pid <= 0) continue;

        if (kill((pid_t)pid, 0) == 0 || errno != ESRCH)
            continue; /* owner still alive — leave it */

        char dir[128];
        snprintf(dir, sizeof(dir), "/tmp/%.120s", de->d_name);

        /* Walk + unlink the dir's contents (all symlinks).  Use a
         * second opendir rather than openat-style iteration to keep
         * this short.  rmdir at the end. */
        DIR *sub = opendir(dir);
        if (sub) {
            struct dirent *se;
            while ((se = readdir(sub)) != NULL) {
                if (strcmp(se->d_name, ".") == 0 || strcmp(se->d_name, "..") == 0)
                    continue;
                /* %.* widths bound each directive so fortify can prove
                 * no truncation — see dlopen_shim's preload helper. */
                char path[256];
                snprintf(path, sizeof(path), "%.127s/%.127s", dir, se->d_name);
                unlink(path);
            }
            closedir(sub);
        }
        if (rmdir(dir) == 0)
            fprintf(stderr, "[antirev] reaped stale symlink dir %s (pid %ld dead)\n",
                    dir, pid);
    }
    closedir(dp);
}

/* Scan /tmp/antirev-deps.log-style graph dump so it's reachable when
 * the daemon's stderr is closed (e.g., launched from sysmgr). */
static void dump_deps_graph_log(const char (*lib_names)[MAX_NAME + 1], int nlibs) {
    FILE *lf = fopen("/tmp/antirev-deps.log", "w");
    if (!lf)
        return;
    fprintf(lf, "# antirev deps graph (%d libs)\n", nlibs);
    for (int i = 0; i < nlibs; i++) {
        fprintf(lf, "%s %d", lib_names[i], g_deps_count[i]);
        for (int k = 0; k < g_deps_count[i]; k++)
            fprintf(lf, " %s", lib_names[g_deps_idx[i][k]]);
        fprintf(lf, "\n");
    }
    fclose(lf);
}

/* Open, bind, and listen on the abstract socket derived from `key`.
 * Returns the listen fd on success, -1 on failure. */
static int daemon_open_listen_socket(const uint8_t *key) {
    struct sockaddr_un addr;
    socklen_t addr_len = make_sock_addr(&addr, key);

    int sd = socket(AF_UNIX, SOCK_STREAM, 0);
    if (sd < 0) {
        perror("socket");
        return -1;
    }
    if (bind(sd, (struct sockaddr *) &addr, addr_len) < 0) {
        perror("[antirev] daemon bind");
        close(sd);
        return -1;
    }
    listen(sd, 16);
    return sd;
}

/* Time `scan_encrypted_libs` and print the decrypt duration. */
static int decrypt_own_libs(const char *real_exe, const uint8_t *key, int *lib_fds, char (*lib_names)[MAX_NAME + 1],
                            int *nlibs) {
    struct timespec t_start, t_end;
    clock_gettime(CLOCK_MONOTONIC, &t_start);
    scan_encrypted_libs(real_exe, key, lib_fds, lib_names, nlibs);
    if (*nlibs == 0) {
        fprintf(stderr, "[antirev] no main binary and no libs found\n");
        return -1;
    }
    clock_gettime(CLOCK_MONOTONIC, &t_end);
    double elapsed = (double) (t_end.tv_sec - t_start.tv_sec) + (double) (t_end.tv_nsec - t_start.tv_nsec) / 1e9;
    fprintf(stderr, "[antirev] decrypted %d libs in %.1fs\n", *nlibs, elapsed);
    return 0;
}

/* Build the per-lib DT_NEEDED adjacency used by OP_GET_CLOSURE so the
 * daemon can return a lib plus its transitive encrypted deps in a
 * single round trip.  Prints a summary + dumps the graph for debug. */
static void build_and_log_deps_graph(int *lib_fds, const char (*lib_names)[MAX_NAME + 1], int nlibs) {
    build_deps_graph(lib_fds, lib_names, nlibs);
    int total_edges = 0, zero = 0;
    for (int i = 0; i < nlibs; i++) {
        total_edges += g_deps_count[i];
        if (g_deps_count[i] == 0)
            zero++;
    }
    fprintf(stderr,
            "[antirev] deps graph: %d libs, %d edges, "
            "%d libs with 0 deps\n",
            nlibs, total_edges, zero);
    dump_deps_graph_log(lib_names, nlibs);
}

/* Run as Mode A: decrypt own libs, build deps graph, daemonize, serve
 * lib fds with clean shutdown on SIGTERM/INT/HUP/QUIT.
 *
 * Daemon process layout (after the daemonising fork):
 *   - Main thread: blocks shutdown signals and sigwait()s; on signal
 *     it pokes the eventfd, joins the worker, closes resources, exits.
 *   - Worker thread: runs the epoll serve loop.  Wakes when the
 *     eventfd fires, returns from daemon_serve.
 *
 * Returns to the caller only on setup failure; on the success path
 * the main thread _exit(0)s after the worker is joined. */
static int run_daemon_forever(const char *real_exe, uint8_t *key, int *lib_fds, char (*lib_names)[MAX_NAME + 1],
                              int *nlibs) {
    raise_fd_limit();

    /* Startup sweep: reap any /tmp/antirev_<pid>_* dirs whose owner
     * is dead.  Catches dirs left behind by protected exes that
     * crashed before exe_shim's atexit could fire. */
    sweep_dead_symlink_dirs();

    if (decrypt_own_libs(real_exe, key, lib_fds, lib_names, nlibs) < 0)
        return 1;

    build_and_log_deps_graph(lib_fds, lib_names, *nlibs);

    int listen_sd = daemon_open_listen_socket(key);
    explicit_bzero(key, KEY_SIZE);
    if (listen_sd < 0)
        return 1;
    fprintf(stderr, "[antirev] lib daemon ready (%d libs)\n", *nlibs);

    pid_t pid = fork();
    if (pid < 0) {
        perror("fork");
        return 1;
    }
    if (pid > 0)
        _exit(0); /* parent: daemon is running */

    /* === child (long-lived daemon) === */
    setsid();
    close(STDIN_FILENO);
    close(STDOUT_FILENO);
    close(STDERR_FILENO);

    /* Process-wide before any thread spawns: ignore SIGPIPE (writes
     * to a closed client socket should EPIPE, not kill us) and
     * register the shutdown-signal mask so both the main thread
     * and the worker inherit it.  Doing this before pthread_create
     * eliminates the race where a fast-arriving SIGPIPE could
     * terminate the process between create and install. */
    signal(SIGPIPE, SIG_IGN);

    sigset_t shutdown_sigs;
    sigemptyset(&shutdown_sigs);
    sigaddset(&shutdown_sigs, SIGTERM);
    sigaddset(&shutdown_sigs, SIGINT);
    sigaddset(&shutdown_sigs, SIGHUP);
    sigaddset(&shutdown_sigs, SIGQUIT);
    if (pthread_sigmask(SIG_BLOCK, &shutdown_sigs, NULL) != 0)
        _exit(1);

    int shutdown_efd = eventfd(0, EFD_CLOEXEC);
    if (shutdown_efd < 0) {
        close(listen_sd);
        _exit(1);
    }

    struct daemon_serve_args args = {
        .listen_fd    = listen_sd,
        .shutdown_efd = shutdown_efd,
        .lib_fds      = lib_fds,
        .lib_names    = lib_names,
        .nlibs        = *nlibs,
    };

    pthread_t worker;
    if (pthread_create(&worker, NULL, daemon_serve_thread, &args) != 0) {
        close(shutdown_efd);
        close(listen_sd);
        _exit(1);
    }

    /* Park the main thread until a shutdown signal arrives. */
    int sig = 0;
    sigwait(&shutdown_sigs, &sig);

    /* Wake the worker.  An eventfd write of 8 bytes is atomic and
     * cannot partial-write; the worker doesn't read the payload, it
     * only needs the fd to become readable. */
    uint64_t one = 1;
    ssize_t  ew  = write(shutdown_efd, &one, sizeof(one));
    (void)ew;

    pthread_join(worker, NULL);

    /* Tear down everything that crossed the thread boundary. */
    close(listen_sd);
    close(shutdown_efd);
    for (int i = 0; i < *nlibs; i++)
        if (lib_fds[i] >= 0) close(lib_fds[i]);

    /* Shutdown sweep: catches any dirs whose owner exe crashed
     * during this daemon's session before its atexit could fire. */
    sweep_dead_symlink_dirs();

    _exit(0);
}

