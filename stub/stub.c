/* ------------------------------------------------------------------ */
/*  Little-endian readers                                              */
/* ------------------------------------------------------------------ */
static inline uint16_t u16le(const uint8_t *p)
{
    return (uint16_t)(p[0] | (p[1] << 8));
}
static inline uint32_t u32le(const uint8_t *p)
{
    return p[0] | ((uint32_t)p[1] << 8) | ((uint32_t)p[2] << 16) | ((uint32_t)p[3] << 24);
}
static inline uint64_t u64le(const uint8_t *p)
{
    uint64_t v = 0;
    for (int i = 0; i < 8; i++) v |= (uint64_t)p[i] << (8 * i);
    return v;
}

/* ------------------------------------------------------------------ */
/*  Write bytes to a new memfd (no O_CLOEXEC — must survive fexecve)  */
/* ------------------------------------------------------------------ */
static int make_memfd(const char *name)
{
    int fd = (int)syscall(__NR_memfd_create, name, 0 /* no MFD_CLOEXEC */);
    if (fd < 0) { perror("memfd_create"); return -1; }
    return fd;
}

static int write_chunk(int fd, const uint8_t *data, size_t len)
{
    for (size_t w = 0; w < len; ) {
        ssize_t n = write(fd, data + w, len - w);
        if (n < 0) { perror("write memfd"); return -1; }
        w += (size_t)n;
    }
    return 0;
}

#define BFLAG_HAS_MAIN 0x01 /* one encrypted main entry follows the header */
#define BFLAG_DAEMON_LIBS 0x02 /* libs served by external daemon */

/* ------------------------------------------------------------------ */
/*  Daemon protocol v2                                                  */
/*                                                                      */
/*  Wire format (per message):                                          */
/*    [u32 op] [u32 payload_len] [payload bytes...]                    */
/*  plus optional SCM_RIGHTS ancillary data (OP_BATCH / OP_LIB only).   */
/*                                                                      */
/*  Client → Daemon:                                                    */
/*    OP_INIT     + [u32 nfilter] [(u16 nlen, bytes) * nfilter]        */
/*                  nfilter=0 means "send all libs".                    */
/*    OP_GET_LIB  + [u16 nlen] [name bytes]                            */
/*    OP_BYE      + (no payload) — daemon closes connection.           */
/*                                                                      */
/*  Daemon → Client (in response to OP_INIT):                           */
/*    Zero or more OP_BATCH messages, each with:                        */
/*      [u32 nlibs] [(u16 nlen, bytes) * nlibs]                        */
/*    and ancillary data: SCM_RIGHTS with nlibs fds.                    */
/*    Followed by a single OP_END message (empty payload) to mark EOF. */
/*                                                                      */
/*  Daemon → Client (in response to OP_GET_LIB):                        */
/*    OP_LIB + [u32 status]                                             */
/*    If status==ST_OK, ancillary SCM_RIGHTS carries 1 fd.              */
/*    If status!=ST_OK, no ancillary data.                              */
/* ------------------------------------------------------------------ */

#define OP_INIT         0x01u
#define OP_GET_LIB      0x02u
#define OP_BYE          0x03u
#define OP_LIST         0x04u  /* request: all lib names (no fds) */
#define OP_GET_CLOSURE  0x05u  /* request: lib + transitive encrypted deps */

#define OP_BATCH    0x81u
#define OP_END      0x82u
#define OP_LIB      0x83u
#define OP_NAMES    0x84u  /* response to OP_LIST: batched name list */

/* Per-lib adjacency cap.  Real business libs can DT_NEED 30+ entries
 * (system + Qt + boost + app libs) and anything beyond this cap gets
 * dropped from the deps graph, which then causes lazy fetch to miss
 * transitive encrypted deps and glibc to fall back to disk (hitting
 * ciphertext and aborting with "invalid elf header").  Keep this
 * well above the largest plausible DT_NEEDED list. */
#define MAX_DEPS_PER_LIB 128

/* Forward decl — defined with the other daemon helpers further below. */
static int compute_closure(const char *start,
                           const char (*lib_names)[MAX_NAME + 1], int nlibs,
                           int *out_idx, int max_out);

#define ST_OK        0u
#define ST_NOT_FOUND 1u
#define ST_ERROR     2u

/* Max payload bytes we will accept or send in one message.
 * Worst case: an OP_BATCH with SCM_BATCH entries, each up to MAX_NAME+2 bytes. */
#define MAX_PAYLOAD  (4u + SCM_BATCH * (2u + MAX_NAME))

static inline void put_u32le(uint8_t *p, uint32_t v)
{
    p[0] = (uint8_t)(v & 0xff);
    p[1] = (uint8_t)((v >> 8) & 0xff);
    p[2] = (uint8_t)((v >> 16) & 0xff);
    p[3] = (uint8_t)((v >> 24) & 0xff);
}

static inline void put_u16le(uint8_t *p, uint16_t v)
{
    p[0] = (uint8_t)(v & 0xff);
    p[1] = (uint8_t)((v >> 8) & 0xff);
}

/* Blocking recv of exactly len bytes. Returns 0 on success, -1 on error/EOF. */
static int recv_full(int sock, void *buf, size_t len)
{
    size_t got = 0;
    while (got < len) {
        ssize_t n = recv(sock, (uint8_t *)buf + got, len - got, 0);
        if (n <= 0) {
            if (n < 0 && errno == EINTR) continue;
            return -1;
        }
        got += (size_t)n;
    }
    return 0;
}

/* Drain all SCM_RIGHTS fds we've already accepted into the caller's
 * array — used to clean up when a recv aborts mid-message after
 * cmsg unpacking succeeded.  Without this, callers see a -1 return
 * and assume nothing arrived; the fds end up stranded until process
 * exit reaps them. */
static void close_received_fds(int *fds, int *nfds)
{
    for (int i = 0; i < *nfds; i++) close(fds[i]);
    *nfds = 0;
}

/* Send one v2 message with optional ancillary fds.
 * Writes [u32 op | u32 plen | payload bytes] and, if nfds > 0,
 * attaches SCM_RIGHTS on the first sendmsg.
 * Returns 0 on success, -1 on failure. */
static int send_msg(int sock, uint32_t op, const void *payload, uint32_t plen,
                    const int *fds, int nfds)
{
    uint8_t hdr[8];
    put_u32le(hdr, op);
    put_u32le(hdr + 4, plen);

    struct iovec iov[2];
    iov[0].iov_base = hdr;
    iov[0].iov_len  = sizeof(hdr);
    iov[1].iov_base = (void *)payload;
    iov[1].iov_len  = plen;

    struct msghdr msg = {0};
    msg.msg_iov    = iov;
    msg.msg_iovlen = (plen > 0) ? 2 : 1;

    char cmsg_buf[CMSG_SPACE(SCM_BATCH * sizeof(int))];
    if (nfds > 0) {
        memset(cmsg_buf, 0, sizeof(cmsg_buf));
        msg.msg_control    = cmsg_buf;
        msg.msg_controllen = CMSG_SPACE((size_t)nfds * sizeof(int));
        struct cmsghdr *cm = CMSG_FIRSTHDR(&msg);
        cm->cmsg_level = SOL_SOCKET;
        cm->cmsg_type  = SCM_RIGHTS;
        cm->cmsg_len   = CMSG_LEN((size_t)nfds * sizeof(int));
        memcpy(CMSG_DATA(cm), fds, (size_t)nfds * sizeof(int));
    }

    size_t total = (size_t)8 + (size_t)plen;
    ssize_t n = sendmsg(sock, &msg, 0);
    if (n < 0) return -1;
    if ((size_t)n == total) return 0;

    /* Partial write after the first sendmsg: ancillary is already
     * delivered, finish the remaining bytes with plain send(). */
    size_t sent = (size_t)n;
    if (sent < 8) {
        size_t hrem = 8 - sent;
        ssize_t m = send(sock, hdr + sent, hrem, 0);
        if (m < 0 || (size_t)m != hrem) return -1;
        sent = 8;
    }
    size_t prem = total - sent;
    if (prem > 0) {
        ssize_t m = send(sock, (const uint8_t *)payload + (sent - 8), prem, 0);
        if (m < 0 || (size_t)m != prem) return -1;
    }
    return 0;
}

/* Receive one v2 message. On success sets *op and *plen, writes up to
 * *plen bytes into payload, and up to max_fds ancillary fds into fds[]
 * (setting *nfds). Returns 0 on success, -1 on error/EOF. */
static int recv_msg(int sock, uint32_t *op,
                    uint8_t *payload, uint32_t *plen, uint32_t max_payload,
                    int *fds, int *nfds, int max_fds)
{
    *nfds = 0;

    /* First recvmsg: 8-byte header + ancillary (if any). */
    uint8_t hdr[8];
    struct iovec iov = { .iov_base = hdr, .iov_len = sizeof(hdr) };

    char cmsg_buf[CMSG_SPACE(SCM_BATCH * sizeof(int))];
    memset(cmsg_buf, 0, sizeof(cmsg_buf));

    struct msghdr msg = {0};
    msg.msg_iov        = &iov;
    msg.msg_iovlen     = 1;
    msg.msg_control    = cmsg_buf;
    msg.msg_controllen = sizeof(cmsg_buf);

    ssize_t got = recvmsg(sock, &msg, 0);
    if (got <= 0) return -1;

    /* Extract any SCM_RIGHTS fds (attached to first byte of message). */
    for (struct cmsghdr *cm = CMSG_FIRSTHDR(&msg); cm; cm = CMSG_NXTHDR(&msg, cm)) {
        if (cm->cmsg_level == SOL_SOCKET && cm->cmsg_type == SCM_RIGHTS) {
            int n = (int)((cm->cmsg_len - CMSG_LEN(0)) / sizeof(int));
            if (n > max_fds) {
                int *src = (int *)CMSG_DATA(cm);
                for (int k = 0; k < n; k++) close(src[k]);
                return -1;
            }
            memcpy(fds, CMSG_DATA(cm), (size_t)n * sizeof(int));
            *nfds = n;
        }
    }

    /* Finish reading the header if we got fewer than 8 bytes. */
    if (got < (ssize_t)sizeof(hdr)) {
        if (recv_full(sock, hdr + got, sizeof(hdr) - (size_t)got) < 0) {
            close_received_fds(fds, nfds);
            return -1;
        }
    }

    *op = u32le(hdr);
    uint32_t p = u32le(hdr + 4);
    if (p > max_payload) {
        close_received_fds(fds, nfds);
        return -1;
    }
    *plen = p;

    if (p > 0) {
        if (recv_full(sock, payload, p) < 0) {
            close_received_fds(fds, nfds);
            return -1;
        }
    }
    return 0;
}

/* Build an OP_BATCH payload (u32 nlibs + per-lib u16 nlen + name bytes). */
static size_t build_batch_payload(uint8_t *buf, size_t buflen,
                                  const char (*names)[MAX_NAME + 1],
                                  int start, int count)
{
    if ((size_t)4 > buflen) return 0;
    put_u32le(buf, (uint32_t)count);
    size_t off = 4;
    for (int i = 0; i < count; i++) {
        uint16_t nlen = (uint16_t)strlen(names[start + i]);
        if (off + 2 + nlen > buflen) return 0;
        put_u16le(buf + off, nlen);
        off += 2;
        memcpy(buf + off, names[start + i], nlen);
        off += nlen;
    }
    return off;
}

/* Send the initial batch (filtered by `filter_names` if nfilter > 0, else all)
 * followed by OP_END. Returns 0 on success, -1 on failure. */
static int send_init_batch(int client, const int *lib_fds,
                           const char (*lib_names)[MAX_NAME + 1], int nlibs,
                           const char (*filter_names)[MAX_NAME + 1], int nfilter)
{
    /* Resolve selection: either the filter list, or the full lib list. */
    int   sel_fds[MAX_FILES];
    int   sel_count = 0;
    char  sel_names[MAX_FILES][MAX_NAME + 1];

    if (nfilter > 0) {
        for (int k = 0; k < nfilter; k++) {
            for (int j = 0; j < nlibs; j++) {
                if (strcmp(filter_names[k], lib_names[j]) == 0) {
                    sel_fds[sel_count] = lib_fds[j];
                    memcpy(sel_names[sel_count], lib_names[j], MAX_NAME + 1);
                    sel_count++;
                    break;
                }
            }
        }
    } else {
        sel_count = nlibs;
        memcpy(sel_fds, lib_fds, (size_t)nlibs * sizeof(int));
        memcpy(sel_names, lib_names, (size_t)nlibs * (MAX_NAME + 1));
    }

    /* Send in SCM_BATCH-sized chunks. */
    for (int off = 0; off < sel_count; off += SCM_BATCH) {
        int batch = sel_count - off;
        if (batch > SCM_BATCH) batch = SCM_BATCH;

        uint8_t payload[MAX_PAYLOAD];
        size_t plen = build_batch_payload(payload, sizeof(payload),
                                          sel_names, off, batch);
        if (plen == 0) return -1;
        if (send_msg(client, OP_BATCH, payload, (uint32_t)plen,
                     sel_fds + off, batch) < 0)
            return -1;
    }
    return send_msg(client, OP_END, NULL, 0, NULL, 0);
}

/* Handle OP_GET_LIB: look up `name` in the daemon's lib table and reply
 * with OP_LIB + status + (optionally) one fd. */
static int handle_get_lib(int client, const int *lib_fds,
                          const char (*lib_names)[MAX_NAME + 1], int nlibs,
                          const uint8_t *payload, uint32_t plen)
{
    uint8_t resp[4];
    if (plen < 2) {
        put_u32le(resp, ST_ERROR);
        return send_msg(client, OP_LIB, resp, 4, NULL, 0);
    }
    uint16_t nlen;
    memcpy(&nlen, payload, 2);
    if ((uint32_t)nlen + 2u > plen || nlen > MAX_NAME) {
        put_u32le(resp, ST_ERROR);
        return send_msg(client, OP_LIB, resp, 4, NULL, 0);
    }
    char name[MAX_NAME + 1];
    memcpy(name, payload + 2, nlen);
    name[nlen] = '\0';

    for (int j = 0; j < nlibs; j++) {
        if (strcmp(lib_names[j], name) == 0) {
            put_u32le(resp, ST_OK);
            int fd = lib_fds[j];
            return send_msg(client, OP_LIB, resp, 4, &fd, 1);
        }
    }
    put_u32le(resp, ST_NOT_FOUND);
    return send_msg(client, OP_LIB, resp, 4, NULL, 0);
}

/* Handle OP_LIST: reply with all lib names in SCM_BATCH-sized OP_NAMES
 * messages, terminated by OP_END. No fds attached. */
static int handle_list(int client,
                       const char (*lib_names)[MAX_NAME + 1], int nlibs)
{
    for (int off = 0; off < nlibs; off += SCM_BATCH) {
        int batch = nlibs - off;
        if (batch > SCM_BATCH) batch = SCM_BATCH;
        uint8_t payload[MAX_PAYLOAD];
        size_t plen = build_batch_payload(payload, sizeof(payload),
                                          lib_names, off, batch);
        if (plen == 0) return -1;
        if (send_msg(client, OP_NAMES, payload, (uint32_t)plen, NULL, 0) < 0)
            return -1;
    }
    return send_msg(client, OP_END, NULL, 0, NULL, 0);
}

/* Handle OP_GET_CLOSURE: compute transitive encrypted closure of the
 * requested lib (via build_deps_graph'd adjacency), then reply with
 * OP_BATCH(es) carrying (fd, name) for every lib in the closure,
 * terminated by OP_END.  If the name is not in the daemon's lib set,
 * replies with a single OP_END (empty closure). */
static int handle_get_closure(int client, const int *lib_fds,
                              const char (*lib_names)[MAX_NAME + 1], int nlibs,
                              const uint8_t *payload, uint32_t plen)
{
    if (plen < 2)
        return send_msg(client, OP_END, NULL, 0, NULL, 0);
    uint16_t nlen;
    memcpy(&nlen, payload, 2);
    if ((uint32_t)nlen + 2u > plen || nlen > MAX_NAME)
        return send_msg(client, OP_END, NULL, 0, NULL, 0);
    char name[MAX_NAME + 1];
    memcpy(name, payload + 2, nlen);
    name[nlen] = '\0';

    int closure[MAX_FILES];
    int n = compute_closure(name, lib_names, nlibs, closure, MAX_FILES);
    if (n <= 0)
        return send_msg(client, OP_END, NULL, 0, NULL, 0);

    /* Materialize ordered fds/names for the batch sender. */
    int  sel_fds[MAX_FILES];
    char sel_names[MAX_FILES][MAX_NAME + 1];
    for (int i = 0; i < n; i++) {
        sel_fds[i] = lib_fds[closure[i]];
        memcpy(sel_names[i], lib_names[closure[i]], MAX_NAME + 1);
    }

    for (int off = 0; off < n; off += SCM_BATCH) {
        int batch = n - off;
        if (batch > SCM_BATCH) batch = SCM_BATCH;
        uint8_t pay[MAX_PAYLOAD];
        size_t pl = build_batch_payload(pay, sizeof(pay), sel_names, off, batch);
        if (pl == 0) return -1;
        if (send_msg(client, OP_BATCH, pay, (uint32_t)pl,
                     sel_fds + off, batch) < 0)
            return -1;
    }
    return send_msg(client, OP_END, NULL, 0, NULL, 0);
}

/* Parse an OP_INIT payload into a filter list. Returns number of names,
 * or -1 on parse error. */
static int parse_init_filter(const uint8_t *payload, uint32_t plen,
                             char (*out_names)[MAX_NAME + 1])
{
    if (plen < 4) return -1;
    uint32_t nfilter = u32le(payload);
    if (nfilter > MAX_FILES) return -1;
    size_t off = 4;
    for (uint32_t i = 0; i < nfilter; i++) {
        if (off + 2 > plen) return -1;
        uint16_t nlen;
        memcpy(&nlen, payload + off, 2);
        off += 2;
        if (nlen > MAX_NAME || off + nlen > plen) return -1;
        memcpy(out_names[i], payload + off, nlen);
        out_names[i][nlen] = '\0';
        off += nlen;
    }
    return (int)nfilter;
}

/* Process a single inbound request from a connected client.
 * Returns 0 to keep the connection alive, -1 to close it. */
static int daemon_handle_request(int client, const int *lib_fds,
                                 const char (*lib_names)[MAX_NAME + 1], int nlibs)
{
    uint32_t op, plen;
    uint8_t  payload[MAX_PAYLOAD];
    int      dummy_fds[1];
    int      dummy_nfds = 0;

    if (recv_msg(client, &op, payload, &plen, MAX_PAYLOAD,
                 dummy_fds, &dummy_nfds, 0) < 0)
        return -1;

    if (op == OP_INIT) {
        char filter[MAX_FILES][MAX_NAME + 1];
        int  nfilter = parse_init_filter(payload, plen, filter);
        if (nfilter < 0) return -1;
        return send_init_batch(client, lib_fds, lib_names, nlibs,
                               filter, nfilter);
    }
    if (op == OP_GET_LIB) {
        return handle_get_lib(client, lib_fds, lib_names, nlibs, payload, plen);
    }
    if (op == OP_LIST) {
        return handle_list(client, lib_names, nlibs);
    }
    if (op == OP_GET_CLOSURE) {
        return handle_get_closure(client, lib_fds, lib_names, nlibs,
                                  payload, plen);
    }
    if (op == OP_BYE) {
        return -1;
    }
    return -1;
}

/* Single-threaded epoll-based serve loop.  Runs as the daemon's worker
 * thread (see run_daemon_forever); the main thread parks in sigwait()
 * and writes to shutdown_efd on SIGTERM/INT/HUP/QUIT.  When the worker
 * sees the eventfd fire it returns, the main thread then joins and
 * tears down everything passed in.
 *
 * Same-uid check enforced at accept.  Client fds are tracked in a
 * fixed-size table so they can all be closed when the loop exits. */
#define DAEMON_MAX_CLIENTS 256

static void daemon_serve(int listen_fd, int shutdown_efd, const int *lib_fds,
                         const char (*lib_names)[MAX_NAME + 1], int nlibs)
{
    uid_t my_uid = getuid();

    int ep = epoll_create1(EPOLL_CLOEXEC);
    if (ep < 0) return;

    struct epoll_event lev = { .events = EPOLLIN, .data.fd = listen_fd };
    if (epoll_ctl(ep, EPOLL_CTL_ADD, listen_fd, &lev) < 0) {
        close(ep);
        return;
    }
    struct epoll_event sev = { .events = EPOLLIN, .data.fd = shutdown_efd };
    if (epoll_ctl(ep, EPOLL_CTL_ADD, shutdown_efd, &sev) < 0) {
        close(ep);
        return;
    }

    int clients[DAEMON_MAX_CLIENTS];
    for (int i = 0; i < DAEMON_MAX_CLIENTS; i++) clients[i] = -1;

    int running = 1;
    struct epoll_event evs[32];
    while (running) {
        int n = epoll_wait(ep, evs, 32, -1);
        if (n < 0) {
            if (errno == EINTR) continue;
            break;
        }

        for (int i = 0; i < n; i++) {
            int fd = evs[i].data.fd;

            if (fd == shutdown_efd) {
                running = 0;
                break;
            }

            if (fd == listen_fd) {
                int client = accept4(listen_fd, NULL, NULL, SOCK_CLOEXEC);
                if (client < 0) continue;

                struct ucred cred;
                socklen_t clen = sizeof(cred);
                if (getsockopt(client, SOL_SOCKET, SO_PEERCRED, &cred, &clen) < 0
                    || cred.uid != my_uid) {
                    close(client);
                    continue;
                }

                struct epoll_event cev = { .events = EPOLLIN | EPOLLRDHUP, .data.fd = client };
                if (epoll_ctl(ep, EPOLL_CTL_ADD, client, &cev) < 0) {
                    close(client);
                    continue;
                }
                /* Track for shutdown close.  Overflow → still served,
                 * just not tracked; the kernel reaps it on _exit. */
                for (int k = 0; k < DAEMON_MAX_CLIENTS; k++) {
                    if (clients[k] == -1) { clients[k] = client; break; }
                }
                continue;
            }

            if (evs[i].events & (EPOLLHUP | EPOLLRDHUP | EPOLLERR)) {
                epoll_ctl(ep, EPOLL_CTL_DEL, fd, NULL);
                close(fd);
                for (int k = 0; k < DAEMON_MAX_CLIENTS; k++) {
                    if (clients[k] == fd) { clients[k] = -1; break; }
                }
                continue;
            }

            if (evs[i].events & EPOLLIN) {
                if (daemon_handle_request(fd, lib_fds, lib_names, nlibs) < 0) {
                    epoll_ctl(ep, EPOLL_CTL_DEL, fd, NULL);
                    close(fd);
                    for (int k = 0; k < DAEMON_MAX_CLIENTS; k++) {
                        if (clients[k] == fd) { clients[k] = -1; break; }
                    }
                }
            }
        }
    }

    /* Shutdown: close every fd we created internally.  listen_fd,
     * shutdown_efd, and lib_fds belong to the caller and are torn
     * down by the main thread after pthread_join. */
    for (int i = 0; i < DAEMON_MAX_CLIENTS; i++)
        if (clients[i] >= 0) close(clients[i]);
    close(ep);
}

struct daemon_serve_args {
    int listen_fd;
    int shutdown_efd;
    const int *lib_fds;
    const char (*lib_names)[MAX_NAME + 1];
    int nlibs;
};

static void *daemon_serve_thread(void *arg)
{
    struct daemon_serve_args *a = (struct daemon_serve_args *)arg;
    daemon_serve(a->listen_fd, a->shutdown_efd, a->lib_fds, a->lib_names, a->nlibs);
    /* If daemon_serve returned (setup error like epoll_create1
     * failure, or normal shutdown after the eventfd fired), make
     * sure the main thread's sigwait() also wakes — otherwise on
     * a worker setup failure the daemon would hang forever waiting
     * for an external signal.  kill(getpid(), SIGTERM) queues a
     * process-targeted signal; SIGTERM is blocked in both threads
     * so it lands wherever sigwait is parked.  Redundant on the
     * normal-shutdown path (main is no longer in sigwait by then),
     * harmless either way. */
    kill(getpid(), SIGTERM);
    return NULL;
}

/* Client-side helper: send OP_INIT (optionally with a filter list),
 * collect all OP_BATCH messages until OP_END, return fds + names.
 * If nfilter == 0, requests the full lib set.
 * Returns 0 on success, -1 on failure. */
static int daemon_request_libs(int sd,
                               const char (*filter_names)[MAX_NAME + 1],
                               int nfilter,
                               int *out_fds,
                               char (*out_names)[MAX_NAME + 1],
                               int *out_nlibs)
{
    /* Build OP_INIT payload: [u32 nfilter] [(u16 nlen, bytes) * nfilter] */
    uint8_t init_payload[MAX_PAYLOAD];
    put_u32le(init_payload, (uint32_t)nfilter);
    size_t off = 4;
    for (int i = 0; i < nfilter; i++) {
        uint16_t nlen = (uint16_t)strlen(filter_names[i]);
        if (off + 2 + nlen > sizeof(init_payload)) return -1;
        put_u16le(init_payload + off, nlen);
        off += 2;
        memcpy(init_payload + off, filter_names[i], nlen);
        off += nlen;
    }
    if (send_msg(sd, OP_INIT, init_payload, (uint32_t)off, NULL, 0) < 0)
        return -1;

    *out_nlibs = 0;
    for (;;) {
        uint32_t op, plen;
        uint8_t  payload[MAX_PAYLOAD];
        int      recvd_fds[SCM_BATCH];
        int      nrecvd = 0;

        if (recv_msg(sd, &op, payload, &plen, MAX_PAYLOAD,
                     recvd_fds, &nrecvd, SCM_BATCH) < 0)
            return -1;

        if (op == OP_END) break;
        /* Any framing / parse failure below this point must close
         * the fds we already accepted via SCM_RIGHTS, otherwise they
         * leak when daemon_connect_and_fetch closes the socket and
         * returns.  out_fds aliases recvd_fds after the memcpy below
         * (same int values, no kernel ref bump), so closing recvd_fds
         * is sufficient. */
        if (op != OP_BATCH) {
            close_received_fds(recvd_fds, &nrecvd);
            return -1;
        }
        if (plen < 4) {
            close_received_fds(recvd_fds, &nrecvd);
            return -1;
        }

        uint32_t nl = u32le(payload);
        if ((int)nl != nrecvd) {
            close_received_fds(recvd_fds, &nrecvd);
            return -1;
        }
        if (*out_nlibs + (int)nl > MAX_FILES) {
            close_received_fds(recvd_fds, &nrecvd);
            return -1;
        }

        memcpy(out_fds + *out_nlibs, recvd_fds, (size_t)nl * sizeof(int));

        size_t poff = 4;
        for (uint32_t i = 0; i < nl; i++) {
            if (poff + 2 > plen) {
                close_received_fds(recvd_fds, &nrecvd);
                return -1;
            }
            uint16_t nlen;
            memcpy(&nlen, payload + poff, 2);
            poff += 2;
            if (nlen > MAX_NAME || poff + nlen > plen) {
                close_received_fds(recvd_fds, &nrecvd);
                return -1;
            }
            memcpy(out_names[*out_nlibs + (int)i], payload + poff, nlen);
            out_names[*out_nlibs + (int)i][nlen] = '\0';
            poff += nlen;
        }
        *out_nlibs += (int)nl;
    }
    return (*out_nlibs > 0) ? 0 : -1;
}

/* Client-side helper: send OP_LIST and collect OP_NAMES/OP_END to recover
 * the full set of encrypted lib names the daemon manages. No fds.
 * Returns 0 on success, -1 on error. */
static int daemon_request_names(int sd,
                                char (*out_names)[MAX_NAME + 1],
                                int *out_nlibs)
{
    if (send_msg(sd, OP_LIST, NULL, 0, NULL, 0) < 0) return -1;
    *out_nlibs = 0;
    for (;;) {
        uint32_t op, plen;
        uint8_t  payload[MAX_PAYLOAD];
        int      dummy_fds[1];
        int      nrecvd = 0;
        if (recv_msg(sd, &op, payload, &plen, MAX_PAYLOAD,
                     dummy_fds, &nrecvd, 0) < 0)
            return -1;
        if (op == OP_END) break;
        if (op != OP_NAMES) return -1;
        if (plen < 4) return -1;
        uint32_t nl = u32le(payload);
        size_t poff = 4;
        for (uint32_t i = 0; i < nl; i++) {
            if (poff + 2 > plen) return -1;
            uint16_t nlen;
            memcpy(&nlen, payload + poff, 2);
            poff += 2;
            if (nlen > MAX_NAME || poff + nlen > plen) return -1;
            if (*out_nlibs >= MAX_FILES) return -1;
            memcpy(out_names[*out_nlibs], payload + poff, nlen);
            out_names[*out_nlibs][nlen] = '\0';
            (*out_nlibs)++;
            poff += nlen;
        }
    }
    return 0;
}

/* Send OP_GET_LIB and receive one OP_LIB response with a single fd.
 * Returns the fd on success, -1 on error. Used by lazy-fetch in dlopen_shim. */
__attribute__((unused))
static int daemon_request_one(int sd, const char *name)
{
    uint16_t nlen = (uint16_t)strlen(name);
    if (nlen > MAX_NAME) return -1;

    uint8_t req[2 + MAX_NAME];
    put_u16le(req, nlen);
    memcpy(req + 2, name, nlen);
    if (send_msg(sd, OP_GET_LIB, req, (uint32_t)(2 + nlen), NULL, 0) < 0)
        return -1;

    uint32_t op, plen;
    uint8_t  payload[16];
    int      recvd_fds[1];
    int      nrecvd = 0;
    if (recv_msg(sd, &op, payload, &plen, sizeof(payload),
                 recvd_fds, &nrecvd, 1) < 0)
        return -1;
    if (op != OP_LIB || plen < 4) {
        close_received_fds(recvd_fds, &nrecvd);
        return -1;
    }
    uint32_t status = u32le(payload);
    if (status != ST_OK || nrecvd != 1) {
        close_received_fds(recvd_fds, &nrecvd);
        return -1;
    }
    return recvd_fds[0];
}

/* Send OP_BYE to cleanly close the daemon socket. Best-effort. */
static void daemon_send_bye(int sd)
{
    (void)send_msg(sd, OP_BYE, NULL, 0, NULL, 0);
}

/* Build abstract socket address from key hash.
 * Returns the sockaddr_un length to pass to bind/connect. */
static socklen_t make_sock_addr(struct sockaddr_un *addr,
                                const uint8_t key[KEY_SIZE])
{
    /* Derive socket name: AES_K(0^128)[0:8] → hex.
     * This is the GHASH subkey H, deterministic per key. */
    aes256gcm_ctx tmp;
    uint8_t dummy_iv[IV_SIZE] = {0};
    aes256gcm_init(&tmp, key, dummy_iv);
    /* tmp.H now contains AES_K(0^128) */

    char hex[17];
    for (int i = 0; i < 8; i++)
        snprintf(hex + i * 2, 3, "%02x", tmp.H[i]);

    memset(addr, 0, sizeof(*addr));
    addr->sun_family = AF_UNIX;
    /* Abstract socket: sun_path[0] = '\0' (already zeroed), name follows */
    snprintf(addr->sun_path + 1, sizeof(addr->sun_path) - 1,
             "antirev_%s", hex);

    explicit_bzero(&tmp, sizeof(tmp));

    return (socklen_t)(offsetof(struct sockaddr_un, sun_path)
                       + 1 + strlen(addr->sun_path + 1));
}

/* ------------------------------------------------------------------ */
/*  ELF DT_NEEDED parser + per-lib dependency graph (daemon-side)     */
/*                                                                      */
/*  At daemon startup we mmap each decrypted lib memfd, parse its      */
/*  PT_DYNAMIC segment, extract DT_NEEDED names, and filter them to    */
/*  the set of encrypted libs the daemon manages.  The resulting       */
/*  adjacency list is later used by compute_closure() to answer        */
/*  OP_GET_CLOSURE requests — so dlopen_shim can fetch a lib and its   */
/*  transitive encrypted deps in a single round-trip.                  */
/* ------------------------------------------------------------------ */

static int  g_deps_count[MAX_FILES];
static int  g_deps_idx[MAX_FILES][MAX_DEPS_PER_LIB];

/* Bounds-check an ELF64 header and phdr table against the mapped size.
 * Returns 0 on a usable ELF64, -1 otherwise. */
static int elf64_header_ok(const Elf64_Ehdr *eh, size_t size) {
    if (memcmp(eh->e_ident, ELFMAG, SELFMAG) != 0)
        return -1;
    if (eh->e_ident[EI_CLASS] != ELFCLASS64)
        return -1;
    if (eh->e_phoff + (uint64_t) eh->e_phnum * eh->e_phentsize > (uint64_t) size)
        return -1;
    return 0;
}

/* Scan program headers for PT_DYNAMIC.  Returns pointer or NULL. */
static const Elf64_Phdr *find_pt_dynamic(const Elf64_Phdr *phdrs, int phnum) {
    for (int i = 0; i < phnum; i++)
        if (phdrs[i].p_type == PT_DYNAMIC)
            return &phdrs[i];
    return NULL;
}

/* Find the DT_STRTAB vaddr in .dynamic.  Returns 0 if absent. */
static uint64_t find_dt_strtab_vaddr(const Elf64_Dyn *dyn, int ndyn) {
    for (int i = 0; i < ndyn; i++) {
        if (dyn[i].d_tag == DT_NULL)
            return 0;
        if (dyn[i].d_tag == DT_STRTAB)
            return dyn[i].d_un.d_ptr;
    }
    return 0;
}

/* Map a runtime vaddr back to its file offset via PT_LOAD segments.
 * Returns -1 if the vaddr isn't covered by any loadable segment. */
static off_t vaddr_to_file_offset(const Elf64_Phdr *phdrs, int phnum, uint64_t vaddr) {
    for (int i = 0; i < phnum; i++) {
        if (phdrs[i].p_type != PT_LOAD)
            continue;
        if (vaddr >= phdrs[i].p_vaddr && vaddr < phdrs[i].p_vaddr + phdrs[i].p_memsz)
            return (off_t) (vaddr - phdrs[i].p_vaddr + phdrs[i].p_offset);
    }
    return -1;
}

/* Walk .dynamic collecting DT_NEEDED names into out[0..max_out-1].
 * Returns the stored count; *total_observed gets the full DT_NEEDED
 * count so the caller can detect truncation. */
static int collect_dt_needed_names(const Elf64_Dyn *dyn, int ndyn, const char *strtab, size_t strtab_max,
                                   char (*out)[MAX_NAME + 1], int max_out, int *total_observed) {
    int n = 0, total = 0;
    for (int i = 0; i < ndyn; i++) {
        if (dyn[i].d_tag == DT_NULL)
            break;
        if (dyn[i].d_tag != DT_NEEDED)
            continue;
        total++;
        if (n >= max_out)
            continue; /* cap reached — keep counting for truncation signal */
        uint64_t off = dyn[i].d_un.d_val;
        if (off >= strtab_max)
            continue;
        size_t len = strnlen(strtab + off, strtab_max - off);
        if (len == 0 || len > MAX_NAME)
            continue;
        memcpy(out[n], strtab + off, len);
        out[n][len] = '\0';
        n++;
    }
    *total_observed = total;
    return n;
}

/* Parse an ELF64 shared lib's DT_NEEDED list from the mapped image.
 * Returns the number of names written, or -1 on parse failure.
 * Caller-facing truncation handling happens in parse_dt_needed(). */
static int parse_dt_needed_mapped(const uint8_t *base, size_t size, char (*out)[MAX_NAME + 1], int max_out,
                                  int *total_observed) {
    const Elf64_Ehdr *eh = (const Elf64_Ehdr *) base;
    if (elf64_header_ok(eh, size) < 0)
        return -1;

    const Elf64_Phdr *phdrs = (const Elf64_Phdr *) (base + eh->e_phoff);
    const Elf64_Phdr *dyn_ph = find_pt_dynamic(phdrs, eh->e_phnum);
    if (!dyn_ph || dyn_ph->p_offset + dyn_ph->p_filesz > (uint64_t) size)
        return -1;

    const Elf64_Dyn *dyn = (const Elf64_Dyn *) (base + dyn_ph->p_offset);
    int ndyn = (int) (dyn_ph->p_filesz / sizeof(Elf64_Dyn));

    uint64_t strtab_vaddr = find_dt_strtab_vaddr(dyn, ndyn);
    if (!strtab_vaddr)
        return -1;

    off_t strtab_off = vaddr_to_file_offset(phdrs, eh->e_phnum, strtab_vaddr);
    if (strtab_off < 0 || strtab_off >= (off_t) size)
        return -1;

    const char *strtab = (const char *) (base + strtab_off);
    size_t strtab_max = size - (size_t) strtab_off;

    return collect_dt_needed_names(dyn, ndyn, strtab, strtab_max, out, max_out, total_observed);
}

/* Parse an ELF64 fd's DT_NEEDED list.  Positive return = full list fits.
 * Negative return = -total (truncation signal: caller should grow
 * MAX_DEPS_PER_LIB).  -1 = parse failure (not ELF64, stripped, etc.). */
static int parse_dt_needed(int fd, char (*out_names)[MAX_NAME + 1], int max_out) {
    struct stat st;
    if (fstat(fd, &st) < 0 || st.st_size < (off_t) sizeof(Elf64_Ehdr))
        return -1;

    void *map = mmap(NULL, (size_t) st.st_size, PROT_READ, MAP_PRIVATE, fd, 0);
    if (map == MAP_FAILED)
        return -1;

    int total = 0;
    int n = parse_dt_needed_mapped((const uint8_t *) map, (size_t) st.st_size, out_names, max_out, &total);
    munmap(map, (size_t)st.st_size);

    if (n < 0)
        return -1;
    return (total > max_out) ? -total : n;
}

/* Build per-lib adjacency list: for each lib, record indices of its
 * direct DT_NEEDED deps that are also in the daemon's lib table.
 * Libs whose deps can't be parsed (stripped, etc.) get deps_count = 0. */
static void build_deps_graph(const int *lib_fds,
                             const char (*lib_names)[MAX_NAME + 1], int nlibs)
{
    char dt_names[MAX_DEPS_PER_LIB][MAX_NAME + 1];
    for (int i = 0; i < nlibs; i++) {
        g_deps_count[i] = 0;
        int n_dt = parse_dt_needed(lib_fds[i], dt_names, MAX_DEPS_PER_LIB);
        if (n_dt == -1) {
            /* Parse failure (no PT_DYNAMIC, ELF32, no DT_STRTAB, etc.)
             * — treat as zero deps.  Common and benign for .elf PG
             * binaries and statically-linked libs. */
            continue;
        }
        if (n_dt < -1) {
            /* Truncation: parse_dt_needed returns -(total) when the
             * total DT_NEEDED count exceeded MAX_DEPS_PER_LIB.  This
             * is bad for lazy closure fetch — any dropped encrypted
             * dep will send glibc to disk at load time and abort
             * with "invalid elf header".  Bump the cap and rebuild. */
            int total = -n_dt;
            fprintf(stderr, "[antirev] WARNING: %s has %d DT_NEEDED "
                    "entries, only %d captured (MAX_DEPS_PER_LIB=%d). "
                    "Increase the cap.\n",
                    lib_names[i], total, MAX_DEPS_PER_LIB,
                    MAX_DEPS_PER_LIB);
            n_dt = MAX_DEPS_PER_LIB;
        }
        if (n_dt == 0) continue;
        for (int k = 0; k < n_dt; k++) {
            for (int j = 0; j < nlibs; j++) {
                if (j == i) continue;
                if (strcmp(lib_names[j], dt_names[k]) == 0) {
                    if (g_deps_count[i] < MAX_DEPS_PER_LIB)
                        g_deps_idx[i][g_deps_count[i]++] = j;
                    break;
                }
            }
        }
    }
}

/* Iterative DFS post-order from `node` through the encrypted-deps graph.
 * Emits each lib AFTER all its transitive deps — giving the client a
 * topological ordering (leaves first, root last) it can replay via
 * real_dlopen() so each lib finds its already-loaded deps in glibc's
 * link map and skips DT_RPATH / DT_RUNPATH search on disk. */
static void dfs_postorder(int start_idx, uint8_t *visited,
                          int *out_idx, int *n_out, int max_out)
{
    /* Stack frame: (node, next-child-index-to-visit).  We push the node
     * when first encountered, keep visiting its unexplored deps, and
     * emit it once the child index runs off the end. */
    int stack_node[MAX_FILES];
    int stack_next[MAX_FILES];
    int top = 0;

    if (visited[start_idx]) return;
    visited[start_idx] = 1;
    stack_node[top] = start_idx;
    stack_next[top] = 0;
    top++;

    while (top > 0) {
        int node = stack_node[top - 1];
        int next = stack_next[top - 1];

        if (next >= g_deps_count[node]) {
            /* All children visited: emit this node and pop. */
            if (*n_out < max_out) out_idx[(*n_out)++] = node;
            top--;
            continue;
        }
        stack_next[top - 1] = next + 1;
        int dep = g_deps_idx[node][next];
        if (!visited[dep] && top < MAX_FILES) {
            visited[dep] = 1;
            stack_node[top] = dep;
            stack_next[top] = 0;
            top++;
        }
    }
}

/* Topological closure from a lib name through the encrypted-deps graph.
 * Writes indices into out_idx[] in deps-first order, returns count
 * (>= 1 on success) or -1 if `start` is not in the lib table. */
static int compute_closure(const char *start,
                           const char (*lib_names)[MAX_NAME + 1], int nlibs,
                           int *out_idx, int max_out)
{
    int start_idx = -1;
    for (int i = 0; i < nlibs; i++) {
        if (strcmp(lib_names[i], start) == 0) { start_idx = i; break; }
    }
    if (start_idx < 0) return -1;

    static uint8_t visited[MAX_FILES];
    memset(visited, 0, sizeof(visited));
    int n_out = 0;
    dfs_postorder(start_idx, visited, out_idx, &n_out, max_out);
    return n_out;
}

/* ------------------------------------------------------------------ */
/*  Decrypt standalone encrypted file from disk                        */
/*  Format: ANTREV01(8) + iv(12) + tag(16) + ciphertext               */
/*  Returns memfd on success, -1 on failure.                           */
/* ------------------------------------------------------------------ */
static int decrypt_enc_file(const char *path, const uint8_t *key,
                            uint8_t *chunk)
{
    int fd = open(path, O_RDONLY);
    if (fd < 0) return -1;

    uint8_t hdr[MAGIC_LEN + IV_SIZE + TAG_SIZE]; /* 36 bytes */
    if (pread(fd, hdr, sizeof(hdr), 0) != (ssize_t)sizeof(hdr)
        || memcmp(hdr, MAGIC, MAGIC_LEN) != 0) {
        close(fd);
        return -1;
    }

    uint8_t *iv  = hdr + MAGIC_LEN;
    uint8_t *tag = hdr + MAGIC_LEN + IV_SIZE;

    off_t fsize = lseek(fd, 0, SEEK_END);
    off_t ct_off = (off_t)sizeof(hdr);
    uint64_t ct_size = (uint64_t)(fsize - ct_off);
    if (ct_size == 0) { close(fd); return -1; }

    const char *bname = strrchr(path, '/');
    bname = bname ? bname + 1 : path;

    int mfd = make_memfd(bname);
    if (mfd < 0) { close(fd); return -1; }

    aes256gcm_ctx ctx;

    /* Single pass: GHASH + CTR simultaneously (halves disk I/O) */
    aes256gcm_init(&ctx, key, iv);
    uint64_t remaining = ct_size;
    off_t pos = ct_off;
    while (remaining > 0) {
        size_t  to_read = (remaining < CHUNK_SIZE) ? (size_t)remaining : CHUNK_SIZE;
        ssize_t got     = pread(fd, chunk, to_read, pos);
        if (got != (ssize_t)to_read) { close(fd); close(mfd); return -1; }
        aes256gcm_onepass(&ctx, chunk, chunk, to_read);
        if (write_chunk(mfd, chunk, to_read) != 0) { close(fd); close(mfd); return -1; }
        pos       += (off_t)to_read;
        remaining -= to_read;
    }
    if (aes256gcm_ghash_verify(&ctx, tag) != 0) {
        if (ftruncate(mfd, 0)) { /* wipe plaintext */ }
        close(fd); close(mfd);
        return -1;
    }
    if (lseek(mfd, 0, SEEK_SET) < 0) { close(fd); close(mfd); return -1; }

    close(fd);
    return mfd;
}

/* ------------------------------------------------------------------ */
/*  Parallel decryption of encrypted .so files from disk               */
/* ------------------------------------------------------------------ */

/* Per-file decryption job */
typedef struct {
    char     path[4096];
    char     name[MAX_NAME + 1];
    const uint8_t *key;
    int      fd;      /* result memfd, -1 on failure */
} dec_job_t;

/* Work-stealing pool: each thread grabs next available job */
typedef struct {
    dec_job_t   *jobs;
    int          njobs;
    volatile int next;   /* atomic counter for work stealing */
} dec_pool_t;

static void *pool_worker(void *arg)
{
    dec_pool_t *pool = (dec_pool_t *)arg;
    uint8_t *chunk = malloc(CHUNK_SIZE);
    if (!chunk) return NULL;

    for (;;) {
        int idx = __sync_fetch_and_add(&pool->next, 1);
        if (idx >= pool->njobs) break;
        pool->jobs[idx].fd = decrypt_enc_file(pool->jobs[idx].path,
                                               pool->jobs[idx].key, chunk);
    }

    explicit_bzero(chunk, CHUNK_SIZE);
    free(chunk);
    return NULL;
}

/* Recursively collect paths of encrypted .so / .elf files (ANTREV01 magic).
 *
 * The extension check is a fast-path filter so we do not open every
 * regular file in the tree to test for our magic — the real
 * authoritative filter is the magic-byte check below.  .elf files
 * are program binaries consumed by ANTI_LoadProcess (served to aarch64_extend_shim
 * via OP_GET_LIB) and coexist with .so libs in the same asset pool. */
static void collect_enc_paths(const char *dir, dec_job_t *jobs, int *njobs,
                              int max)
{
    DIR *dp = opendir(dir);
    if (!dp) return;

    struct dirent *de;
    while ((de = readdir(dp)) != NULL && *njobs < max) {
        if (de->d_name[0] == '.') continue;

        char path[4096];
        snprintf(path, sizeof(path), "%s/%s", dir, de->d_name);

        struct stat st;
        if (lstat(path, &st) != 0) continue;
        if (S_ISLNK(st.st_mode)) continue;  /* skip symlinks — avoid dupes */

        if (S_ISDIR(st.st_mode)) {
            collect_enc_paths(path, jobs, njobs, max);
            continue;
        }
        if (!S_ISREG(st.st_mode)) continue;
        if (!strstr(de->d_name, ".so") && !strstr(de->d_name, ".elf")) continue;

        /* Skip duplicate filenames (same lib in different subdirs) */
        int dup = 0;
        for (int i = 0; i < *njobs; i++) {
            if (strcmp(jobs[i].name, de->d_name) == 0) { dup = 1; break; }
        }
        if (dup) continue;

        /* Quick magic check — avoids queuing non-encrypted files */
        int fd = open(path, O_RDONLY);
        if (fd < 0) continue;
        uint8_t magic[MAGIC_LEN];
        int is_enc = (read(fd, magic, MAGIC_LEN) == MAGIC_LEN
                      && memcmp(magic, MAGIC, MAGIC_LEN) == 0);
        close(fd);
        if (!is_enc) continue;

        snprintf(jobs[*njobs].path, sizeof(jobs[0].path), "%s", path);
        snprintf(jobs[*njobs].name, sizeof(jobs[0].name), "%s", de->d_name);
        jobs[*njobs].fd = -1;
        (*njobs)++;
    }
    closedir(dp);
}

/* Scan directory, decrypt all encrypted libs in parallel using pthreads */
static int scan_encrypted_libs(const char *exe_path, const uint8_t *key,
                               int *lib_fds, char (*lib_names)[MAX_NAME + 1],
                               int *nlibs)
{
    char dir[4096];
    const char *slash = strrchr(exe_path, '/');
    if (slash) {
        size_t dlen = (size_t)(slash - exe_path);
        if (dlen >= sizeof(dir)) return -1;
        memcpy(dir, exe_path, dlen);
        dir[dlen] = '\0';
    } else {
        dir[0] = '.'; dir[1] = '\0';
    }

    /* Phase 1: collect encrypted file paths */
    dec_job_t *jobs = calloc(MAX_FILES, sizeof(dec_job_t));
    if (!jobs) return -1;
    int njobs = 0;
    collect_enc_paths(dir, jobs, &njobs, MAX_FILES);

    if (njobs == 0) { free(jobs); return 0; }

    /* Phase 2: decrypt in parallel */
    long ncpus = sysconf(_SC_NPROCESSORS_ONLN);
    if (ncpus < 1) ncpus = 4;
    int nthreads = (njobs < (int)ncpus) ? njobs : (int)ncpus;

    for (int i = 0; i < njobs; i++)
        jobs[i].key = key;

    dec_pool_t pool = { .jobs = jobs, .njobs = njobs, .next = 0 };
    pthread_t *tids = calloc((size_t)nthreads, sizeof(pthread_t));
    if (!tids) { free(jobs); return -1; }

    for (int t = 0; t < nthreads; t++)
        pthread_create(&tids[t], NULL, pool_worker, &pool);
    for (int t = 0; t < nthreads; t++)
        pthread_join(tids[t], NULL);

    /* Phase 3: collect results */
    for (int i = 0; i < njobs && *nlibs < MAX_FILES; i++) {
        if (jobs[i].fd >= 0) {
            lib_fds[*nlibs] = jobs[i].fd;
            memcpy(lib_names[*nlibs], jobs[i].name, MAX_NAME + 1);
            (*nlibs)++;
            fprintf(stderr, "[antirev] decrypted: %s\n", jobs[i].name);
        }
    }

    free(tids);
    free(jobs);
    return 0;
}

