/* Read the first 20 bytes of `fd` and return non-zero if its ELF
 * e_machine differs from the host arch — i.e. we need QEMU to run it. */
static int needs_qemu_for_fd(int fd) {
    uint8_t hdr[20];
    if (pread(fd, hdr, sizeof(hdr), 0) != sizeof(hdr))
        return 0;
    if (hdr[0] != 0x7f || hdr[1] != 'E' || hdr[2] != 'L' || hdr[3] != 'F')
        return 0;
    uint16_t e_machine = hdr[18] | ((uint16_t) hdr[19] << 8);
#if defined(__x86_64__) || defined(__i386__)
    return e_machine != 0x3E && e_machine != 0x03; /* not x86_64/x86 */
#elif defined(__aarch64__)
    return e_machine != 0xB7; /* not aarch64 */
#else
    (void) e_machine;
    return 0;
#endif
}

/* Build argv for a QEMU exec: [binname, /proc/self/fd/N, <original argv[1..]>, NULL].
 * Caller owns the returned allocation. */
static char **build_qemu_argv(const char *binname, const char *fd_path, char *const *argv, int orig_argc) {
    char **out = malloc((size_t) (orig_argc + 2) * sizeof(char *));
    if (!out)
        return NULL;
    out[0] = (char *) binname;
    out[1] = (char *) fd_path;
    for (int i = 1; i < orig_argc; i++)
        out[i + 1] = argv[i];
    out[orig_argc + 1] = NULL;
    return out;
}

/* Try each well-known QEMU path in order; execve on the first hit.
 * Returns only on failure (execve on success never returns). */
static void try_exec_known_qemu(char **qemu_argv, char **new_env) {
    static const char *qemu_candidates[] = {"/tmp/.antirev-qemu-aarch64", "/usr/bin/qemu-aarch64-static",
                                            "/usr/bin/qemu-aarch64", NULL};
    for (int qi = 0; qemu_candidates[qi]; qi++) {
        struct stat st;
        if (stat(qemu_candidates[qi], &st) == 0)
            execve(qemu_candidates[qi], qemu_argv, new_env);
    }
}

/* Buffered copy of an fd.  Returns 0 on success, -1 on any write error. */
static int copy_fd_to_fd(int src, int dst) {
    char buf[8192];
    ssize_t n;
    while ((n = read(src, buf, sizeof(buf))) > 0) {
        ssize_t off = 0;
        while (off < n) {
            ssize_t w = write(dst, buf + off, (size_t) (n - off));
            if (w <= 0)
                return -1;
            off += w;
        }
    }
    return n < 0 ? -1 : 0;
}

/* Probe /proc/<pid>/exe for up to `timeout_ms` (polled at 20ms) looking
 * for a path containing "qemu".  Returns the resolved path in `out` on
 * success (NUL-terminated), or returns -1 on timeout. */
static int wait_for_qemu_exe(pid_t pid, char *out, size_t out_sz, int timeout_ms) {
    char proc_exe[64];
    snprintf(proc_exe, sizeof(proc_exe), "/proc/%d/exe", (int) pid);
    for (int i = 0; i < timeout_ms / 20; i++) {
        usleep(20000);
        ssize_t n = readlink(proc_exe, out, out_sz - 1);
        if (n > 0) {
            out[n] = '\0';
            if (strstr(out, "qemu"))
                return 0;
        }
    }
    return -1;
}

/* Probe-cache path: fork a child through binfmt_misc, copy its
 * /proc/<pid>/exe (= the QEMU binary the kernel chose) to /tmp so
 * we can exec it directly next time.  Handles Docker where QEMU
 * isn't on PATH (binfmt_misc F-flag keeps it kernel-side only).
 *
 * Returns 0 if the cache file is populated; -1 otherwise. */
static int cache_qemu_from_probe(int main_fd, char *const *argv, char **new_env) {
    pid_t probe = fork();
    if (probe == 0) {
        fexecve(main_fd, argv, new_env);
        _exit(127);
    }
    if (probe < 0)
        return -1;

    char qemu_path[256];
    int found = wait_for_qemu_exe(probe, qemu_path, sizeof(qemu_path), 1000);
    int cached = -1;
    if (found == 0) {
        char proc_exe[64];
        snprintf(proc_exe, sizeof(proc_exe), "/proc/%d/exe", (int) probe);
        int src = open(proc_exe, O_RDONLY);
        if (src >= 0) {
            int dst = open("/tmp/.antirev-qemu-aarch64", O_WRONLY | O_CREAT | O_TRUNC, 0755);
            if (dst >= 0) {
                if (copy_fd_to_fd(src, dst) == 0)
                    cached = 0;
                close(dst);
            }
            close(src);
        }
    }

    kill(probe, SIGKILL);
    int st;
    waitpid(probe, &st, 0);
    return cached;
}

/* Phase 6 dispatcher.  On native execution: fexecve(main_fd).  On
 * cross-arch: try cached QEMU, then known system paths, then fall
 * back to binfmt_misc-probe-and-cache, then re-exec through the
 * cached QEMU.  Any failure falls through to a direct fexecve — the
 * kernel's own binfmt handling is the last line of defence. */
static void exec_target(int main_fd, char *const *argv, char **new_env, const char *real_exe) {
    if (!needs_qemu_for_fd(main_fd)) {
        fexecve(main_fd, (char *const *) argv, new_env);
        return;
    }

    const char *binname = strrchr(real_exe, '/');
    binname = binname ? binname + 1 : real_exe;

    int orig_argc = 0;
    while (argv[orig_argc])
        orig_argc++;

    char fd_path[32];
    snprintf(fd_path, sizeof(fd_path), "/proc/self/fd/%d", main_fd);

    char **qemu_argv = build_qemu_argv(binname, fd_path, argv, orig_argc);
    if (qemu_argv) {
        try_exec_known_qemu(qemu_argv, new_env);
        free(qemu_argv);
    }

    if (cache_qemu_from_probe(main_fd, argv, new_env) == 0) {
        char **re_argv = build_qemu_argv(binname, fd_path, argv, orig_argc);
        if (re_argv) {
            execve("/tmp/.antirev-qemu-aarch64", re_argv, new_env);
            free(re_argv);
        }
    }

    fexecve(main_fd, (char *const *) argv, new_env);
}

/* ------------------------------------------------------------------ */
/*  main                                                               */
/* ------------------------------------------------------------------ */
int main(int argc __attribute__((unused)), char *argv[], char *envp[])
{
    /* 1. Capture real exe path before fexecve replaces /proc/self/exe.
     *    Use raw syscall to bypass any inherited LD_PRELOAD exe_shim from
     *    a parent protected binary — we need the true kernel-level path. */
    char real_exe[4096];
    {
        ssize_t n = (ssize_t)syscall(SYS_readlinkat, AT_FDCWD,
                                     "/proc/self/exe", real_exe, sizeof(real_exe) - 1);
        if (n <= 0) { perror("readlink /proc/self/exe"); return 1; }
        real_exe[n] = '\0';
    }

    /* 2. Open /proc/self/exe and read the 48-byte trailer */
    int self = open("/proc/self/exe", O_RDONLY);
    if (self < 0) { perror("open /proc/self/exe"); return 1; }

    off_t fsize = lseek(self, 0, SEEK_END);
    if (fsize < 48) {
        fprintf(stderr, "[antirev] binary too small\n");
        return 1;
    }

    /* Trailer layout: [bundle_offset:8B][key:32B][magic:8B] = 48 bytes */
    uint8_t trailer[48];
    if (pread(self, trailer, 48, fsize - 48) != 48) {
        perror("pread trailer"); return 1;
    }
    if (memcmp(trailer + 40, MAGIC, MAGIC_LEN) != 0) {
        fprintf(stderr, "[antirev] not a protected binary (magic not found)\n");
        return 1;
    }

    uint64_t bundle_off = u64le(trailer);

    /* 2. Extract key from trailer */
    uint8_t key[KEY_SIZE];
    memcpy(key, trailer + 8, KEY_SIZE);

    /* ----------------------------------------------------------------
     * Phase 1: scan bundle headers — collect metadata for every file.
     * We only read the tiny per-file headers; ciphertext is skipped via
     * offset arithmetic and never loaded into user-space memory here.
     * ---------------------------------------------------------------- */
    uint8_t tmp[2 + MAX_NAME + 1 + IV_SIZE + TAG_SIZE + 8]; /* max header */

    /* Read bundle_flags (1 byte) */
    if (pread(self, tmp, 1, (off_t) bundle_off) != 1) {
        perror("pread bundle_flags");
        return 1;
    }
    uint8_t bundle_flags = tmp[0];

    file_entry_t main_entry;
    int have_main = (bundle_flags & BFLAG_HAS_MAIN) != 0;
    off_t scan = (off_t) bundle_off + 1;

    if (have_main) {
        file_entry_t *e = &main_entry;

        /* name_len (2 bytes) */
        if (pread(self, tmp, 2, scan) != 2) { perror("pread name_len"); return 1; }
        uint16_t nlen = u16le(tmp);
        if (nlen > MAX_NAME) {
            fprintf(stderr, "[antirev] name too long in bundle\n");
            return 1;
        }
        scan += 2;

        /* name + iv + tag + ct_size */
        ssize_t hdr_rest = nlen + IV_SIZE + TAG_SIZE + 8;
        if (pread(self, tmp, (size_t)hdr_rest, scan) != hdr_rest) {
            perror("pread file header"); return 1;
        }
        memcpy(e->name, tmp, nlen);
        e->name[nlen]  = '\0';
        memcpy(e->iv, tmp + nlen, IV_SIZE);
        memcpy(e->tag, tmp + nlen + IV_SIZE, TAG_SIZE);
        e->ct_size = u64le(tmp + nlen + IV_SIZE + TAG_SIZE);
        scan          += hdr_rest;

        e->ct_offset   = (uint64_t)scan;
        scan          += (off_t)e->ct_size;

        /* Sanity: ciphertext must not overlap the trailer */
        if (e->ct_offset + e->ct_size > (uint64_t)(fsize - 48)) {
            fprintf(stderr, "[antirev] bundle extends past trailer\n");
            return 1;
        }
    }

    /* Parse needed-libs section (after file entries, when BFLAG_DAEMON_LIBS).
     * Format: [num_needed:2B] [name_len:2B name:...]... */
    int n_needed = 0;
    int has_needed_section = 0;
    char needed_names[MAX_FILES][MAX_NAME + 1];

    if (bundle_flags & BFLAG_DAEMON_LIBS) {
        has_needed_section = 1;
        uint8_t nbuf[2];
        if (pread(self, nbuf, 2, scan) == 2) {
            uint16_t nn = u16le(nbuf);
            scan += 2;
            for (int i = 0; i < (int)nn && i < MAX_FILES; i++) {
                if (pread(self, nbuf, 2, scan) != 2) break;
                uint16_t nl = u16le(nbuf);
                scan += 2;
                if (nl > MAX_NAME) break;
                if (pread(self, needed_names[n_needed], nl, scan) != nl) break;
                needed_names[n_needed][nl] = '\0';
                n_needed++;
                scan += nl;
            }
        }
    }

    /* ----------------------------------------------------------------
     * Phase 2: for each file, single-pass GHASH + CTR decryption.
     * Reads ciphertext once, writes plaintext to memfd, then verifies
     * the authentication tag.  On auth failure, plaintext is wiped.
     *
     * Peak user-space heap: one CHUNK_SIZE buffer.
     * ---------------------------------------------------------------- */
    uint8_t *chunk = malloc(CHUNK_SIZE);
    if (!chunk) { perror("malloc chunk"); return 1; }

    int main_fd = -1;
    int lib_fds[MAX_FILES];
    char lib_names[MAX_FILES][MAX_NAME + 1];
    int nlibs = 0;

    if (have_main) {
        file_entry_t *e = &main_entry;
        aes256gcm_ctx ctx;

        /* Single pass: GHASH + CTR simultaneously (halves I/O) */
        int fd = make_memfd(e->name);
        if (fd < 0) return 1;

        aes256gcm_init(&ctx, key, e->iv);
        uint64_t remaining = e->ct_size;
        off_t    pos       = (off_t)e->ct_offset;
        while (remaining > 0) {
            size_t  to_read = (remaining < CHUNK_SIZE) ? (size_t)remaining : CHUNK_SIZE;
            ssize_t got     = pread(self, chunk, to_read, pos);
            if (got != (ssize_t)to_read) { perror("pread ciphertext"); return 1; }
            aes256gcm_onepass(&ctx, chunk, chunk, to_read);
            if (write_chunk(fd, chunk, to_read) != 0) return 1;
            pos       += (off_t)to_read;
            remaining -= to_read;
        }
        if (aes256gcm_ghash_verify(&ctx, e->tag) != 0) {
            fprintf(stderr, "[antirev] decryption failed for '%s'\n", e->name);
            if (ftruncate(fd, 0)) { /* wipe plaintext */ }
            close(fd);
            return 1;
        }
        if (lseek(fd, 0, SEEK_SET) < 0) { perror("lseek memfd"); return 1; }

        main_fd = fd;
    }

    /* Done reading from the binary */
    explicit_bzero(chunk, CHUNK_SIZE);
    free(chunk);
    close(self);

    /* Mode A: no main binary → run as a lightweight lib daemon forever. */
    if (main_fd < 0)
        return run_daemon_forever(real_exe, key, lib_fds, lib_names, &nlibs);

    /* Phase 3. Mode B (daemon libs): fetch lib fds from the daemon. */
    int  daemon_sd = -1;
    char all_enc_names[MAX_FILES][MAX_NAME + 1];
    int  n_all_enc = 0;

    if ((bundle_flags & BFLAG_DAEMON_LIBS) && nlibs == 0) {
        if (fetch_libs_from_daemon(real_exe, envp, key, has_needed_section, n_needed, needed_names, lib_fds, lib_names,
                                   &nlibs, all_enc_names, &n_all_enc, &daemon_sd) < 0)
            return 1;
    }

    /* Wipe key (after daemon socket name derivation) */
    explicit_bzero(key, sizeof(key));

    /* Phase 4. Write embedded shims to memfds.
     *
     * antirev_shim.so hosts both the exe-identity interceptors (readlink
     * / realpath / getauxval …) and the dlopen interceptor — one memfd,
     * one LD_PRELOAD entry.  The aarch64 extend shim stays separate and
     * is materialised below only when building for __aarch64__. */
    int antirev_shim_fd = make_memfd("antirev_shim.so");
    if (antirev_shim_fd < 0)
        return 1;
    if (write_chunk(antirev_shim_fd, antirev_shim_blob, antirev_shim_blob_len) != 0)
        return 1;
    if (lseek(antirev_shim_fd, 0, SEEK_SET) < 0) {
        perror("lseek antirev_shim");
        return 1;
    }

    /* Phase 4b. Split libs into DT_NEEDED (symlink dir) and rest (ANTIREV_FD_MAP).
     *
     * DT_NEEDED libs are resolved by glibc's normal BFS through the symlink
     * dir on LD_LIBRARY_PATH — preserving the original symbol lookup order.
     * They are NOT put on LD_PRELOAD (which would reorder them ahead of
     * unencrypted libs and change symbol resolution).
     *
     * Remaining libs are available on-demand via dlopen_shim + ANTIREV_FD_MAP. */
    int dt_needed_fds[MAX_FILES];
    char dt_needed_names[MAX_FILES][MAX_NAME + 1];
    int n_dt_needed = 0;

    int fdmap_fds[MAX_FILES];
    char fdmap_names[MAX_FILES][MAX_NAME + 1];
    int n_fdmap = 0;

    if (has_needed_section && nlibs > 0) {
        /* Identify which daemon libs are in the exe's DT_NEEDED chain.
         * These go into the symlink dir only (not LD_PRELOAD). */
        for (int k = 0; k < n_needed; k++) {
            for (int j = 0; j < nlibs; j++) {
                if (strcmp(lib_names[j], needed_names[k]) == 0) {
                    dt_needed_fds[n_dt_needed] = lib_fds[j];
                    memcpy(dt_needed_names[n_dt_needed], lib_names[j], MAX_NAME + 1);
                    n_dt_needed++;
                    break;
                }
            }
        }
        /* Remaining libs go to ANTIREV_FD_MAP for on-demand dlopen */
        for (int j = 0; j < nlibs; j++) {
            int is_needed = 0;
            for (int k = 0; k < n_needed; k++) {
                if (strcmp(lib_names[j], needed_names[k]) == 0) {
                    is_needed = 1;
                    break;
                }
            }
            if (!is_needed) {
                fdmap_fds[n_fdmap] = lib_fds[j];
                memcpy(fdmap_names[n_fdmap], lib_names[j], MAX_NAME + 1);
                n_fdmap++;
            }
        }
        fprintf(stderr, "[antirev] split: %d via symlinks, %d on FD_MAP "
                "(needed=%d, daemon=%d)\n",
                n_dt_needed, n_fdmap, n_needed, nlibs);
        for (int k = 0; k < n_dt_needed; k++)
            fprintf(stderr, "[antirev]   DT_NEEDED: %s (fd %d)\n",
                    dt_needed_names[k], dt_needed_fds[k]);
    } else {
        /* No needed list — all libs go on LD_PRELOAD (backward compat).
         * This path is used by old protected binaries without the needed
         * section.  It does NOT preserve original symbol lookup order. */
        n_dt_needed = nlibs;
        memcpy(dt_needed_fds, lib_fds, (size_t)nlibs * sizeof(int));
        memcpy(dt_needed_names, lib_names, (size_t)nlibs * (MAX_NAME + 1));
        fprintf(stderr, "[antirev] backward compat: all %d libs on LD_PRELOAD\n",
                nlibs);
    }

    /* Phase 4c. Create symlink dir for ALL encrypted libs (DT_NEEDED + FD_MAP):
     *   /tmp/antirev_XXXXXX/libfoo.so → /proc/self/fd/N
     *
     * This dir is prepended to LD_LIBRARY_PATH.  For DT_NEEDED libs, this
     * is the PRIMARY resolution mechanism — glibc's BFS finds them here,
     * preserving the original symbol lookup order.  For FD_MAP (dlopen'd)
     * libs, the symlinks serve as a fallback when the lib's own DT_NEEDED
     * chain references other encrypted libs. */
    /* PID-tagged template lets the daemon's startup/shutdown sweep
     * identify which symlink dirs are owned by which protected exe.
     * If our PID is dead by the time the daemon next sweeps, the dir
     * gets reaped — covers the SIGKILL / segfault / hard-crash path
     * that exe_shim's atexit handler can't.  mkdtemp replaces the
     * trailing XXXXXX with random chars and chmods the dir to 0700. */
    char link_dir[64];
    snprintf(link_dir, sizeof(link_dir), "/tmp/antirev_%d_XXXXXX", (int)getpid());
    char *link_dir_ptr = mkdtemp(link_dir);
    if (link_dir_ptr) {
        for (int j = 0; j < n_dt_needed; j++) {
            char lpath[sizeof(link_dir) + MAX_NAME + 2];
            char target[64];
            snprintf(lpath, sizeof(lpath), "%s/%s", link_dir, dt_needed_names[j]);
            snprintf(target, sizeof(target), "/proc/self/fd/%d", dt_needed_fds[j]);
            if (symlink(target, lpath) < 0)
                perror("[antirev] symlink");
        }
        for (int j = 0; j < n_fdmap; j++) {
            char lpath[sizeof(link_dir) + MAX_NAME + 2];
            char target[64];
            snprintf(lpath, sizeof(lpath), "%s/%s", link_dir, fdmap_names[j]);
            snprintf(target, sizeof(target), "/proc/self/fd/%d", fdmap_fds[j]);
            if (symlink(target, lpath) < 0)
                perror("[antirev] symlink");
        }
        fprintf(stderr, "[antirev] symlink dir: %s (%d links)\n",
                link_dir, n_dt_needed + n_fdmap);
    }

    /* Phase 5. Build the envp we'll hand fexecve. */
    char **new_env = build_exec_env(&(exec_env_cfg_t) {
            .envp = envp,
            .real_exe = real_exe,
            .main_fd = main_fd,
            .antirev_shim_fd = antirev_shim_fd,
            .has_needed_section = has_needed_section,
            .nlibs = nlibs,
            .n_dt_needed = n_dt_needed,
            .dt_needed_fds = dt_needed_fds,
            .n_fdmap = n_fdmap,
            .fdmap_names = fdmap_names,
            .fdmap_fds = fdmap_fds,
            .daemon_sd = daemon_sd,
            .n_all_enc = n_all_enc,
            .all_enc_names = all_enc_names,
            .link_dir_ptr = link_dir_ptr,
            .link_dir = link_dir,
    });
    if (!new_env) {
        perror("malloc env");
        return 1;
    }

    /* Phase 6. Replace this process with the decrypted binary. */
    exec_target(main_fd, argv, new_env, real_exe);
    perror("fexecve");
    return 1;
}
